            <div class="boxSec nohover">
                <h4><strong>Q 1. What is the difference between Insolvency and Bankruptcy?</strong>
                </h4>
                <p>An entity (individual, or a company) becomes
                    insolvent when they are unable to pay back their lenders on time. Simply, when
                    the assets fall short to pay off the existing liabilities.</p>
                <p>Whereas,&nbsp;<strong>Bankruptcy</strong>&nbsp;is a
                    legal declaration of one’s inability to pay their debts. On filing of
                    bankruptcy, the entity is obligated to pay whatever it owes to its lenders which
                    can be done in two ways:</p>
                <p>1. Restructuring- where the debt is ‘re- structured’ or re- planned to make
                    repayment easier.</p>
                <p>2. Liquidation- where all the
                    assets of the entity are sold and paid to debtors.
                </p>
                <h5><strong>Thus, Insolvency is a state of being that
                        might result in an entity filing for bankruptcy.</strong></h5>
                <h4><strong>Q 2. When is IBC, 2016 applicable?</strong>
                </h4>
                <p>According to Section 2, the provisions of the Code
                    shall apply for insolvency, liquidation, voluntary liquidation or bankruptcy of
                    the following entities: –</p>
                <p>(a) Any company</p>
                <p>(b) Any Limited Liability Partnership</p>
                <p>(c) Personal Guarantors to Corporate Debtors</p>
                <p>(d) Partnership firms and Proprietorship firms;</p>
                <p>(e) Individuals (Other than personal guarantors)</p>
                <p>(f) Such other body incorporated under any law for
                    the time being in force, as the Central Government may by notification specify
                    in this behalf.</p>
                <h4><strong>Q 3. What is the meaning of Default under
                        IBC?</strong></h4>
                <p>Default means a non-payment of debt when whole or
                    any part or installment of the amount of debt has&nbsp;<strong>become due and
                        payable and is not paid</strong>&nbsp;by the debtor or the corporate debtor,
                    as the case may be</p>
                <h4><strong>Q 4. What is the threshold limit for
                        initiation of corporate insolvency resolution process?</strong></h4>
                <p>Rs 1 crore and above w.e.f. 24.03.2020 (Previously,
                    it was Rs 1 Lakh and above)</p>
                <h4><strong>Q 5. Who can initiate the insolvency
                        proceedings?</strong></h4>
                <p>The corporate insolvency resolution process may be
                    initiated against any defaulting corporate debtor by</p>
                <p>(a) Financial creditor – e.g. Individual money
                    lender, banks, NBFCs,etc where money is lent (Section 7)</p>
                <p>(b) Operational creditor e.g. supplier of goods
                    or/and services (Section 9)</p>
                <p>(c) Corporate debtor – a company/LLP who owes debt
                    to any person (Section 10)</p>
                <h4><strong>Q 6. What is the meaning of Financial
                        creditor?</strong></h4>
                <p>Section 5(7) of the Code
                    defines&nbsp;<strong>Financial creditor</strong>&nbsp;– means any person to
                    whom&nbsp;<strong>financial</strong>&nbsp;debt is owed and includes a person to
                    whom such debt has been legally assigned or transferred to. For example, Banks,
                    Financial Institution, NBFC, any other person who lends / provide finance and
                    Home Buyers(in case Real estate projects).</p>
                <h4><strong>Q 7. What is the meaning of Operational
                        creditor?</strong></h4>
                <p>Operational Creditor is defined under section 5
                    (20), it means, a person to whom an operational debt is owed and includes any
                    person to whom such debt has been legally assigned or transferred.</p>
                <p>Where, Operational Debt means a claim in respect of</p>
                <p>(a) provision of goods, or</p>
                <p>(b) provision of services including employment, or
                </p>
                <p>(c) any dues payable to the government</p>
                <h4><strong>Q 8. What is the meaning of Corporate
                        debtor?</strong></h4>
                <p>Corporate Debtor means a corporate person who owes a
                    debt to any person. In simple terms, who accepted / taken money or loan from any
                    person.</p>
                <h4><strong>Q 9. Is there any time limit for completion
                        of the Insolvency Resolution Process?</strong></h4>
                <p>Section 12 of the Code states that any Insolvency
                    Resolution Process shall be completed within a period of&nbsp;<strong>180
                        days</strong>&nbsp;from the date of admission of the application to initiate
                    the process. However, the NCLT may provide&nbsp;<strong>one extension which
                        shall not extend more than 90 days.</strong></p>
                <h4><strong>&nbsp;Q 10. </strong><strong>Who is an
                        insolvency professional?</strong></h4>
                <p>An Insolvency Professional is one who is registered
                    with the&nbsp;<strong>Insolvency and Bankruptcy
                        Board of India (IBBI)</strong>. They are enrolled with an Insolvency
                    Agency and they are involved in the dissolution process of an insolvent
                    individual, companies, LLPs or partnerships. These professionals are authorized
                    to act on behalf of such insolvent individual, companies etc. During the
                    bankruptcy situation, the insolvency professionals play a vital role in
                    liquidating the entity assets and other settlement processes.</p>
                <h4><strong>Q 11. Why is IBC a code and not an
                        act?</strong></h4>
                <p>The <strong>Insolvency and
                        Bankruptcy&nbsp;Code, 2016</strong>(<strong>IBC</strong>) is the
                    bankruptcy law of India which seeks to consolidate the existing framework
                    (SARFAESI Act,2002; SICA,1985; DRT,1993, etc.) by creating a single law for
                    insolvency and bankruptcy. Thus, IBC is like a summation of all existing acts.
                </p>
