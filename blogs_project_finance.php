                        <h4>Financing an industrial project goes typically through the following 3 stages:</h4>

                        <p><strong>Pre-Finance stage:</strong> This stage involves realizing the project plan to be
                            pursued basis business needs and market trends followed by a detailed analysis of the risks
                            involved and concluding if the project is technically, politically, economically, and
                            ecologically viable.</p>

                        <p><strong>Financing stage:</strong> In this stage, the sponsor as an SPV avails equity and loan
                            from financial bodies and arrives at a mutually documented agreement of the payment terms
                            and the loan amount, which is then injected into the SPV to finance the project. The
                            disbursement takes place with a mix of debt and equity with a ratio generally of 70:30 (may
                            vary from case to case). After Project financing is complete it enters the financial close
                            stage</p>

                        <p><strong>Post-Financing stage: </strong>Once the project is fully financed, the project-cycle
                            and its milestones are kept under close watch and pushed for completion ahead of the
                            ascertained deadline. Upon project completion, the loans are paid off through the project
                            earnings.</p>
