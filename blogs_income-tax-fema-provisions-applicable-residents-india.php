                <h4><strong>PROVISIONS OF INCOME-TAX LAW AND FEMA USEFUL FOR
                        NON-RESIDENTS</strong></h4>
                <p>In this part you can gain knowledge about various provisions of
                    Income-tax Law and <strong>Foreign Exchange Management Act, 1999</strong> (FEMA)
                    which are useful to a non-resident.</p>
                <p>The first part deals with provisions of Income-tax Law and the second
                    part deals with the provisions of FEMA.</p>
                <h4><strong>Different classes of residential status prescribed under the
                        Income-tax Law for an individual</strong></h4>
                <p>For the purpose of Income-tax Law, an individual may have any one of the
                    following residential status:</p>
                <p>(1) Resident and ordinarily resident in India</p>

                <p>(2) Resident but not ordinarily resident
                    in India</p>
                <p>(3) Non-resident</p>
                <p>Every year the residential status of the taxpayer is to be
                    determined by applying the provisions of the Income-tax Law designed in this regard
                    (discussed later) and, hence, it may so happen that in one year the individual would be
                    a resident and ordinarily resident and in the next year he may become non-resident or
                    resident but not ordinarily resident and again in the next year his status may change or
                    may remain same.</p>
                <h4><strong>Different classes of residential status prescribed
                        under the Income-tax Law for a Hindu Undivided Family (HUF)</strong></h4>
                <p>For the purpose of Income-tax Law, an HUF may have any one
                    of the following residential status:</p>
                <p>(1) Resident and ordinarily resident in
                    India</p>

                <p>(2) Resident but not ordinarily resident
                    in India</p>
                <p>(3) Non-resident</p>
                <p>Every year the residential status of the taxpayer is to be
                    determined by applying the provisions of the Income-tax Law designed in this regard
                    (discussed later) and, hence, it may so happen that in one year the HUF would be a
                    resident and ordinarily resident and in the next year it may become non-resident or
                    resident but not ordinarily resident and again in the next year its status may change or
                    may remain same.</p>
                <h4<strong>Different classes of residential status prescribed
                    under the Income -tax Law for a person other than an individual or a HUF</strong>
                    </p>
                    <p>For the purpose of Income-tax Law, a person other than an
                        individual or a HUF, i.e., company, partnership firm, etc., may have any one of the
                        following residential status:</p>
                    <p>(1) Resident</p>
                    <p>(2) Non-resident</p>
                    <p>Every year the residential status of the taxpayer is to be
                        determined by applying the provisions of the Income-tax Law designed in this regard
                        (discussed later) and, hence, it may so happen that in one year the taxpayer would be a
                        resident and in the next year the taxpayer may become non-resident and again in the next
                        year the status may change or may remain same.</p>
                    <h4><strong>Determination of the residential status of an
                            Individual</strong></h4>
                    <p>To determine the residential status of an individual, the
                        first step is to ascertain whether he is resident or non-resident. If he turns to be a
                        resident, then the next step is to ascertain whether he is resident and ordinarily
                        resident or is a resident but not ordinarily resident.</p>
                    <p>Step 1 given below will ascertain whether the individual is
                        resident or non-resident; and step 2 will ascertain whether he is ordinarily resident or
                        not ordinarily resident. Step 2 is to be performed only if the individual turns to be a
                        resident in India.</p>
                    <p><strong>Step 1: Determining whether resident or
                            non-resident</strong></p>
                    <p>Under the Income-tax Law, an individual will be treated as a
                        resident in India for a year if he satisfies any of the following conditions (i.e. may
                        satisfy any one or may satisfy both the conditions):</p>
                    <p>1) He is in India for a period of 182
                        days or more in that year; or</p>
                    <p>2) He is in India for a period of 60
                        days or more in the year and for a period of 365 days or more in immediately preceding 4
                        years.</p>
                    <p>However, in respect of an Indian citizen and a person of
                        Indian origin who visits India during the year, the period of 60 days as mentioned in
                        (2) above shall be substituted with 182 days. The similar concession is provided to the
                        Indian citizen who leaves India in any previous year as a crew member or for the purpose
                        of employment outside India.</p>
                    <p>The Finance Act, 2020, w.e.f., Assessment Year 2021-22 has
                        amended the above exception to provide that the period of 60 days as mentioned in (2)
                        above shall be substituted with 120 days, if an Indian citizen or a person of Indian
                        origin whose total income, other than income from foreign sources, exceeds Rs. 15 lakhs
                        during the previous year. Income from foreign sources means income which accrues or
                        arises outside India (except income derived from a business controlled in or a
                        profession set up in India).</p>
                    <p><strong>Note: </strong>The Finance Act, 2020 has introduced
                        new section 6(1A) to the Income-tax</p>
                    <p>Act, 1961. The new provision provides that an Indian citizen
                        shall be deemed to be resident in India only if his total income, other than income from
                        foreign sources, exceeds Rs. 15 lakhs during the previous year. For this provision,
                        income from foreign sources means income which accrues or arises outside India (except
                        income derived from a business controlled in or a profession set up in India).</p>
                    <p>However, such individual shall be deemed to be Indian
                        resident only when he is not liable to tax in any country or jurisdiction by reason of
                        his domicile or</p>
                    <p>residence or any other criteria of similar nature.</p>
                    <p>Thus, from Assessment Year 2021-22, an Indian Citizen
                        earning total income in excess of Rs. 15 lakhs (other than from foreign sources) shall
                        be deemed to be resident in India if he is not liable to pay tax in any country.</p>
                    <p>“Liable to tax” in relation to a person and with reference
                        to a country means that there is an income-tax liability on such person under the law of
                        that country for</p>
                    <p>the time being in force. It shall include a person who has
                        subsequently been exempted from such liability under the law of that country.</p>
                    <p>If an individual does not satisfy any of the above
                        conditions then he will be treated as non-resident in India.</p>
                    <p>Step 2: Determining whether resident and ordinarily resident
                        or resident but not ordinarily resident</p>
                    <p>A resident individual will be treated as resident and
                        ordinarily resident in India during the year if he satisfies the following conditions:
                    </p>
                    <p>1) He is resident in India for at least
                        2 years out of 10 years immediately preceding the relevant year; or</p>
                    <p>2) His stay in India is for 730 days or
                        more during 7 years immediately preceding the relevant year.</p>
                    <p>However, w.e.f., Assessment Year 2021-22, the <strong>Finance Act, 2020</strong> has inserted
                        the following two more situations wherein a resident person is deemed to be ‘Not
                        Ordinarily Resident’ in India:</p>
                    <p>a) An Indian Citizen or a person of
                        Indian origin whose total income (other than income from foreign sources) exceeds Rs. 15
                        lakhs during the previous year and who has been in India for a period of 120 days or
                        more but less than 182 days;</p>
                    <p>b) An Indian Citizen who is deemed to be
                        resident in India as per new Section 6(1A).</p>
                    <p>A resident individual who does not satisfy any of the
                        aforesaid conditions or satisfies only one of the aforesaid conditions will be treated
                        as resident but not ordinarily resident.</p>
                    <p>In short, following test will determine the residential
                        status of an individual:</p>
                    <p>1. If the individual satisfies any one
                        or both the conditions specified at step 1 and satisfies any of the conditions specified
                        at step 2, then he will become resident and ordinarily resident in India.</p>
                    <p>2. If the individual satisfies any one
                        or both the conditions specified at step 1 and satisfies none or one condition specified
                        at step 2, then he will become resident but not ordinarily resident in India.</p>
                    <p>3. If the individual satisfies none of
                        the conditions specified at step one, then he will become non-resident.</p>
                    <h4><strong>Determination of the residential status of a
                            HUF</strong></h4>
                    <p>To determine the residential status of a HUF, the first step
                        is to ascertain whether the HUF is resident or a non-resident. If the HUF turns to be a
                        resident, then the next step is to ascertain whether it is resident and ordinarily
                        resident or is resident but not ordinarily resident. Step 1 given below will ascertain
                        whether the HUF is resident or non-resident and step 2 will ascertain whether the HUF is
                        ordinarily resident or not ordinarily resident. Step 2 is to be performed only if the
                        HUF turns to be a resident in India.</p>
                    <p>Step 1: Determining whether resident or non-resident</p>
                    <p>For the purpose of Income-tax Law, an HUF will be treated as
                        resident in India, if the control and management of the affairs of the HUF is located
                        (partly or wholly) in India.</p>
                    <p>Step 2: Determining whether resident and ordinarily resident
                        or resident but not ordinarily resident</p>
                    <p>A resident HUF will be treated as resident and ordinarily
                        resident in India during the year if its manager (i.e. karta or manager) satisfies both
                        the following conditions:</p>
                    <p>(1) He is resident in India for at least
                        2 years out of 10 years immediately preceding the relevant year.</p>
                    <p>(2) His stay in India is for 730 days or
                        more during 7 years immediately preceding the relevant year.</p>
                    <p>A resident HUF whose manager (i.e. karta or manager) does
                        not satisfy any of the aforesaid conditions or satisfies only one of the aforesaid
                        conditions will be treated as resident but not ordinarily resident.</p>
                    <p>In short, following test will determine the residential
                        status of a HUF :</p>
                    <p>1. If the control and management of the
                        affairs of the HUF is located (partly or wholly) in India and the manager (i.e. karta or
                        manager) satisfies both the conditions specified at step 2, then the HUF will become
                        resident and ordinarily resident in India.</p>
                    <p>2. If the control and management of the
                        affairs of the HUF is located (partly or wholly) in India and the manager (i.e. karta or
                        manager) satisfies none or only one condition specified at step 2, then the HUF will
                        become resident but not ordinarily resident in India.</p>
                    <p>3. If the control and management of the
                        affairs of the HUF is located wholly outside India, then the HUF will become
                        non-resident.</p>
                    <h4><strong>Determination of the residential status of a
                            company</strong></h4>
                    <p>A company incorporated in India will always be considered as
                        resident of India.</p>
                    <p>A company other than an Indian company (i.e., a foreign
                        company) is said to be resident in India during a year, if its place of effective
                        management, in that year, is in India.</p>
                    <p>Place of effective management (POEM) means a place where key
                        management and commercial decision that are necessary for conduct of the business of an
                        entity as a whole are in substance made.</p>
                    <p>The concept of POEM is effective from Assessment Year
                        2017-18. Therefore, the CBDT has recently issued the final guidelines for determination
                        of POEM of a foreign company.</p>
                    <p>The final guidelines on POEM contain some unique features.
                        One of the unique features is test of Active Business Outside India (ABOI). The
                        guidelines prescribe that a company shall be said to engaged in ‘active business outside
                        India’ if passive income is not more than 50% of its total income. Further, there are
                        certain additional cumulative conditions to be satisfied regarding location of total
                        assets, employees and payroll expenses.</p>
                    <p>The place of effective management in case of a company
                        engaged in active business outside India shall be presumed to be outside India if the
                        majority meetings of the board of directors of the company are held outside India.</p>
                    <p>In cases of companies other than those that are engaged in
                        active business outside India, the determination of POEM would be a two stage process,
                        namely:—</p>
                    <p>(i) First stage would be identification
                        or ascertaining the person or persons who actually make the key management and
                        commercial decision for conduct of the company’s business as a whole.</p>
                    <p>(ii) Second stage would be determination
                        of place where these decisions are in fact being made.</p>
                    <p>However, it has been provided that the POEM guidelines shall
                        not apply to a company having turnover or gross receipts of INR 50 crores or less in a
                        financial year vide <em>CIRCULAR NO.8, DATED 23-2-2017.</em></p>
                    <p>(To know more about POEM guidelines, read <em>CIRCULAR NO.6,
                            DATED 24-1 -201 7.)</em></p>
                    <h4><strong>Determination of the residential status of person
                            other than an individual, HUF and company</strong></h4>
                        <p>Every person other than an individual, HUF and company is
                            said to be resident in India during the year, if the control and management of its
                            affairs for that year is located wholly or partly in India.</p>
