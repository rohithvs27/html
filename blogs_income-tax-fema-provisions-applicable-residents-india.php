                                <h4><strong>Provisions of FEMA useful for non-residents</strong>
                                </h4>
                                <p><strong>Objectives of FEMA</strong></p>
                                <p>FEMA stands for Foreign Exchange Management Act, 1999. In
                                    this part you can gain knowledge about important provisions of FEMA 1999.</p>
                                <p>The main objective of FEMA is to facilitate external trade
                                    and payments and for promoting the orderly development and maintenance of foreign
                                    exchange market in India. FEMA deals with provisions relating to procedures,
                                    formalities, dealings, etc. of foreign exchange transactions in India. The
                                    transactions
                                    relating to foreign exchange have been classified under FEMA into two main
                                    categories,
                                    viz., (1) Current Account Transaction, (2) Capital Account Transaction.</p>
                                <h4><strong>Meaning of capital account transaction</strong></h4>
                                <p>As defined in Section 2(e) of the FEMA, “capital account
                                    transaction” means a transaction which alters the assets or liabilities, including
                                    contingent liabilities, outside India of persons resident in India or assets or
                                    liabilities in India of persons resident outside India, and includes transactions
                                    referred to in sub-section (3) of section 6.</p>
                                <p>Subject to the provisions as laid down by RBI in
                                    consultation with Central Government, any person may sell or draw foreign exchange
                                    to or
                                    from an authorised person for a capital account transaction.</p>
                                <p>The Reserve Bank may, in consultation with the Central
                                    Government, specify—</p>
                                <p>(a) any class or classes of capital
                                    account transactions, involving debt instruments/not involving debt instruments,
                                    which
                                    are permissible;</p>
                                <p>(b) the limit up to which foreign
                                    exchange shall be admissible for such transactions;</p>
                                <p>(c) any conditions which may be placed
                                    on such transactions:</p>
                                <p><strong>Provided </strong><em>that the Reserve Bank or the
                                        Central Government shall not impose any restrictions on the drawal of foreign
                                        exchange for payment due on account of amortisation of loans or for depreciation
                                        of
                                        direct investments in the ordinary course of business</em></p>
                                <p>The Central Government may, in consultation with the Reserve
                                    Bank, prescribe—</p>
                                <p>(a) any class or classes of capital
                                    account transactions, not involving debt instruments, which are permissible;</p>
                                <p>(b) the limit up to which foreign
                                    exchange shall be admissible for such transactions; and</p>
                                <p>(c) any conditions which may be placed
                                    on such transactions.</p>
                                <p>A person resident in India may hold, own, transfer or invest
                                    in foreign currency, foreign security or any immovable property situated outside
                                    India
                                    if such currency, security or property was acquired, held or owned by such person
                                    when
                                    he was <strong>resident outside India </strong>or inherited from a person who was
                                    <strong>resident outside India</strong></p>
                                <p>A person resident outside India may hold, own, transfer or
                                    invest in Indian currency, security or any immovable property situated in India if
                                    such
                                    currency, security or property was acquired, held or owned by such person when he
                                    was
                                    <strong>resident in India </strong>or inherited from a person who was
                                    <strong>resident
                                        in India</strong></p>
                                <p>Without prejudice to the provisions of this section, the
                                    Reserve Bank may, by regulation, prohibit, restrict, or regulate establishment in
                                    India
                                    of a branch, office or other place of business by a person resident outside India,
                                    for
                                    carrying on any activity relating to such branch, office or other place of business
                                </p>
                                <h4><strong>Major provisions covered in FEMA, 1999</strong></h4>
                                <p>The major provisions of FEMA, 1999 relate to following
                                    matters :</p>
                                <p>1. Dealing in foreign exchange, etc.</p>
                                <p>2. Holding of foreign exchange, etc.</p>
                                <p>3. Current account transactions</p>
                                <p>4. Capital account transactions</p>
                                <p>5. Export of goods and services</p>
                                <p>6. Realization and repatriation of
                                    foreign exchange</p>
                                <p>7. Exemption from realization and
                                    repatriation in certain cases.</p>
                                <p>8. Provisions relating to authorised
                                    persons. i.e. authorised by RBI to deal with foreign exchange or in foreign
                                    securities
                                </p>
                                <p>9. Power of RBI to inspect authorized
                                    person</p>
                                <p>10. Contravention and penalties</p>
                                <p>11. Adjudication and appeal</p>
