                        <h4><strong>Incomes which are deemed to be received in
                                India</strong></p>
                            <p>Following incomes are treated as incomes deemed to be
                                received in India:</p>
                            <p>1. Interest credited to recognised
                                provident fund account of an employee in excess of 9.5% per annum.</p>
                            <p>2. Employer’s contribution to recognised
                                provident fund in excess of 12%.</p>
                            <p>3. Transferred balance in case of
                                reorganisation of unrecognised provident fund.</p>
                            <p>4. Contribution by the Central
                                Government or other employer to the account of the employee in case of notified pension
                                scheme referred to in section 80CCD.</p>
                            <h4><strong>Incomes which are deemed to accrue or arise in
                                    India</strong></h4>
                            <p>Following incomes are treated as incomes deemed to accrue or
                                arise in India:</p>
                            <p>1. Capital gain arising on transfer of
                                property situated in India.</p>
                            <p>2. Income from business connection (to
                                be discussed in later part) in India (*).</p>
                            <p>3. Income from salary in respect of
                                services rendered in India.</p>
                            <p>4. Salary received by an Indian national
                                from Government of India in respect of service rendered outside India. However,
                                allowances and perquisites are exempt in this case.</p>
                            <p>5. Income from any property, asset or
                                other source of income located in India.</p>
                            <p>6. Dividend paid by an Indian company.
                            </p>
                            <p>7. Interest received from Government of
                                India.</p>
                            <p>8. Interest received from a resident is
                                treated as income deemed to accrue or arise in India in all cases, except where such
                                interest is earned in respect of funds borrowed by the resident and is used for carrying
                                on business/profession outside India or is in respect of funds borrowed by the resident
                                and is used for earning income from any source outside India.</p>
                            <p>9. Interest received from a non-resident
                                is treated as income deemed to accrue or arise in India if such interest is earned in
                                respect of funds borrowed by the non­resident for carrying on any business/profession in
                                India.</p>
                            <p>10. Royalty/fees for technical services
                                received from Government of India.</p>
                            <p>11. Royalty/fees for technical services
                                received from resident is treated as income deemed to accrue or arise in India in all
                                cases, except where such royalty/fees relates to business/profession/other source of
                                income carried on by the payer outside India.</p>
                            <p>12. Royalty/fees for technical services
                                received from non-resident is treated as income deemed to accrue or arise in India if
                                such royalty/fees is received for business/profession/other source of income carried on
                                by the payer in India.</p>
                            <p>13. With effect from September 1, 2019,
                                Section 201 has been amended to extend the benefit to a deductor even in respect of
                                failure to deduct tax from sum paid to non-resident.</p>
                            <p>In case of non-resident, being a person engaged in business
                                of banking any interest payable by the permanent establishment in India of such
                                non-resident to the head-office or any permanent establishment or any other part of such
                                non-resident outside India shall be deemed to accrue or arise in India.</p>
                            <h4><strong>Meaning of business connection</strong></p>
                                <p>Business connection shall include following business
                                    activities carried out by a person acting on behalf of a non-resident:</p>
                                <p>1. If a person has and habitually exercises in India, an
                                    authority to conclude contracts on behalf of non-resident or habitually concludes
                                    contracts or habitually plays the principal role leading to conclusion of contracts
                                    by
                                    that non-resident and the contracts are:</p>
                                <p>a) in the name of the non-resident; or
                                </p>
                                <p>b) for the transfer of the ownership of,
                                    or for the granting of the right to use, property owned by that non-resident or that
                                    non-resident has the right to use; or</p>
                                <p>c) for the provision of services by the
                                    non-resident</p>
                                <p>2. If such person has no authority to conclude contracts but
                                    he habitually maintains in India a stock of goods or merchandise from which he
                                    regularly
                                    delivers goods or merchandise on behalf of the non-resident; or</p>
                                <p>3. If such person habitually secures orders in India mainly
                                    or wholly for the non­resident or for other non-residents under the same management.
                                </p>
                                <p>No business connection shall be deemed to have been
                                    established, if the business is carried on through an independent broker, general
                                    commission agent or other agent (i.e., a broker or commission agent who is not
                                    working
                                    mainly or wholly for such non-resident or other non-residents under same
                                    management),
                                    provided such person is working in his ordinary course of business.</p>
                                <p>Only so much of income which accrues or arises due to such
                                    business connection is deemed to be income accruing or arising from India and not
                                    the
                                    entire income of the non­resident.</p>
                                <p>Further, the significant economic presence of a non-resident
                                    in India shall constitute “business connection” in India. The “significant economic
                                    presence” shall mean:</p>
                                <p>a) transaction in respect of any goods,
                                    services or property carried out by a non­resident in India including provision of
                                    download of data or software in India, if the aggregate of payments arising from
                                    such
                                    transaction or transactions during the previous year exceeds such amount as may be
                                    prescribed; or</p>
                                <p>b) Systematic and continuous soliciting
                                    of business activities or engaging in interaction with such number of users as may
                                    be
                                    prescribed, in India through digital means:</p>
                                <p>Only so much of income as is attributable to the
                                    transactions or activities referred to in clause (a) or clause (b) shall be deemed
                                    to
                                    accrue or arise in India.</p>
                                <p>The transactions or activities shall constitute significant
                                    economic presence in India, whether or not the agreement is entered in India or
                                    non-resident has a residence or place of business in India or renders services in
                                    India.
                                </p>
