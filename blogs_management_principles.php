                <p>14 management principles by Henri Fayol are universally accepted guidelines for managers to do their
                    job according to their responsibility.</p>
                <p>14 management principles are;</p>
                <ol>
                    <li>Division of Work.</li>
                    <li>Balancing Authority and Responsibility.</li>
                    <li>Discipline.</li>
                    <li>Unity of Command.</li>
                    <li>Unity of Direction.</li>
                    <li>Subordination of Individual Interests to the General Interest.</li>
                    <li>Remuneration.</li>
                    <li>Centralization.</li>
                    <li>Scalar Chain.</li>
                    <li>Order.</li>
                    <li>Equity.</li>
                    <li>Stability of Tenure of Personnel.</li>
                    <li>Initiative.</li>
                    <li>Esprit De Corps.</li>
                </ol>
                <p>Henri Fayol, a French industrialist, is now recognized as the Father of Modern Management. In the
                    year 1916 Fayol wrote a book entitled “Industrial and General Administration”. In this book, he gave
                    the 14 Principles of Management.</p>
                <p><img 
                        src="assets/img/blog/management-principles.webp"
                        alt="14 Management Principles by Henri Fayol" data-recalc-dims="1" width="453" height="426"></p>
                <h4>1. Division of Work</h4>
                <p>Dividing the full work of the organization
                        among individuals and creating departments is called the division of work.</p>
                <p>Division of work leads to specialization, and specialization helps to increases efficiency and
                    efficiency which results in improvements in the productivity and profitability of the organization.
                </p>
                <h4>2. Balancing Authority and Responsibility</h4>
                <p>Authority must be equal to Responsibility.</p>
                <p>According to Henri Fayol, there should be a balance between Authority (Power) and
                        Responsibility (Duties). The right to give orders should not be considered without reference
                    to responsibility.</p>
                <p>If the authority is more than responsibility then chances are that a manager may misuse it. If
                    responsibility is more than authority then he may feel frustrated.</p>
                <h4>3. Discipline</h4>
                <p>Outward mark of respect in accordance with formal or informal agreements between a firm and its
                    employees.</p>
                <p>Discipline means respect for the rules and regulations of the organization. Discipline may be
                    Self-discipline, or it may be Enforced discipline.</p>
                <p>No slacking or bending of rules, not allowed in any organization. The works must respect the rules
                    that run the organization. To establish discipline, good supervision and impartial judgment are
                    needed.</p>
                <h4>4. Unity of Command</h4>
                <p>According to this principle, a subordinate (employee) must have and receive orders from only one
                    superior (boss or manager).</p>
                <p>To put it another way, a subordinate must report to only one superior. It helps in preventing dual
                    subordination. This decreases the possibilities of “Dual subordination” which creates a problem is a
                    function of managers.</p>
                <h4>5. Unity of Direction</h4>
                <p>One head and one plan for a group of activities with the same objective. All activities which have
                    the same objective must be directed by one manager, and he must use one plan.</p>
                <p>This is called the Unity of Direction.</p>
                <p>For example, all marketing activities such as advertising, sales promotion, pricing policy, etc.,
                    must be directed by only one manager.</p>
                <p>He must use only one plan for all the marketing activities. Unity of direction means
                        activities aimed at the same objective should be organized so that there are one plan and
                    one person in charge.</p>
                <h4>6. Subordination of Individual Interests to the General Interest</h4>
                <p>The interest of one individual or one group should not prevail over the general good. The individual
                    interest should be given less importance, while the general interest should be given the most
                    importance.</p>
                <p>If not, the organization will collapse. The interest of the organizational goal should not be
                    sabotaged by the interest of an individual or on the group.</p>
                <h4>7. Remuneration</h4>
                <p>Remuneration is the price for services received. Pay should be fair to both the employee and the
                    firm.</p>
                <p>If an organization wants efficient employees and best performance, then it should have a good
                    remuneration policy.</p>
                <p>This policy should give maximum satisfaction to both employers and employees. It should include both
                    financial and non-financial incentives.</p>
                <p>Compensation should be based on a
                        systematic attempt to reward good performance.</p>
                <h4>8. Centralization</h4>
                <p>It is always present to a greater or lesser extent, depending on the size of the company and the
                    quality of its managers. In centralization,
                        the authority is concentrated only in a few hands.</p>
                <p>However, in decentralization, the authority is distributed to all the levels of management. No
                    organization can be completely centralized or decentralized.</p>
                <p>If there is complete centralization, then the subordinates will have no authority (power) to carry
                    out their responsibility (duties). Similarly, if there is complete decentralization, then the
                    superior will have no authority to control the organization.</p>
                <p>Therefore, there
                        should be a balance between centralization and decentralization</a>.</p>
                <p>The degree to which
                        centralization or decentralization should be adopted depends on the specific organization,
                    but managers should retain final responsibility but should give subordinates enough authority to do
                    the tasks successfully.</p>
                <h4>9. Scalar Chain</h4>
                <p>The chain of command, sometimes called the scalar chain, is the formal line of authority,
                    communication, and responsibility within an organization.</p>
                <p>The chain of command is usually
                        depicted on an organizational chart, which identifies the superior and subordinate
                    relationships in the organizational structure.</p>
                <p>Or it is the line of authority from top to bottom of the organization. This chain implements the
                    unity-of-command principle and allows the orderly flow of information.</p>
                <p>Under the unity of command principle, the instructions flow downward along the chain of command and
                    accountability flows upward.</p>
                <p>More clear-cut the chain of command, the more effective the
                        decision-making process and the greater the efficiency.</p>
                <h4>10. Order</h4>
                <p>A place for everything and everything in its place’ the right man in the right place. There should be
                    an Order for material/things and people in the organization.</p>
                <p>Order for things is called Material Order and order for people is called ‘Social Order’. Material
                    Order refers to “a place for everything and everything in its place.”</p>
                <p>Social Order refers to the selection of the “right man in the right place”.</p>
                <p>There must be an orderly placement of the resources such as Men and Women, Money, Materials, etc.
                    Human and material resources must be in the right place at the right time. Misplacement will lead to
                    misuse and disorder.</p>
                <h4>11. Equity</h4>
                <p>While dealing with the employees a manager should use kindliness and justice towards employees
                    equally. Equity is a combination of kindness and justice.</p>
                <p>It creates loyalty and devotion in the employees toward the organization. The equity principle
                    suggests that the managers must be kind as well as equally fair to the subordinates.</p>
                <h4>12. Stability of Tenure of Personnel</h4>
                <p>Although it could take a lot of time, Employees need to be given fair enough time to settle into
                    their jobs. An employee needs time to learn his job and to become efficient.</p>
                <p>The employees should have job security because instability leads to inefficiency. Successful firms
                    usually had a stable group of employees.</p>
                <h4>13. Initiative</h4>
                <p>Without limits of authority and discipline, all levels of staff should be encouraged to show
                    initiative. Management should encourage initiative.</p>
                <p>That is, they should encourage the employees to make their own plans and to execute these plans. This
                    is because an initiative gives satisfaction to the employees and brings success to the organization.
                </p>
                <p>It allows the subordinates to think out a plan and do what it takes to make it happen.</p>
                <h4>14. Esprit De Corps</h4>
                <p>Esprit de Corps means “Team Spirit”. Therefore, the management should create unity, co-operation, and
                    team-spirit among the employees.</p>
                <p>They should avoid dividing and rule policy. Harmony, cohesion among personnel. It’s a great source of
                    strength in the organization. It is a quality in every successful business.</p>
                <p>These principles are guidelines for every management function. The manager must act according to the
                    14 principles of management; in order to reach the goal and create a surplus.</p>
                <p>These 14 management principles of Henri Fayol are universally accepted. they work as a guideline for
                    managers to do their job according to their responsibility.</p>
