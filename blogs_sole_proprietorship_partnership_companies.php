                <h2>Company</h2>
                <p>A company is a legal entity that people form. It allows them, usually as shareholders, to buy and
                    sell goods and services. In most cases, companies want to make a profit. However, some companies are
                    non-profit organizations.</p>
                <p>Just like humans, companies can hire and fire workers. They can also buy shares in other companies,
                    sue, and be the target of a lawsuit. Additionally, companies can borrow and lend money, and own
                    property.</p>
                <p>As legal entities, companies have similar rights and responsibilities to people. They may be
                    responsible for human rights violations. They may also exercise human rights issues against
                    individuals or the state.</p>
                <h4>Characteristics of a Company</h4>
                <p>The company has several distinct characteristics; the significant ones are discussed here:</p>
                <h5>Separate Legal Entity</h5>
                <p>A company is a separate legal entity from its members who constitute it. It can hold, purchase and
                    sell properties and enter into contracts in its own name. It is an artificial legal person who can
                    sue aid be sued. Companies are owned by shareholders and they elect the Board of Directors, who run
                    the company. The board, in turn, selects the management. Thus the shareholders exercise only
                    indirect control over the affairs of the company. The separation of ownership from the management
                    some-times results in a conflict of interests between owners and management. The best the
                    shareholders can do is to change some of the directors through a vote in the annual general meeting
                    subsequent to any such conflict.</p>
                <h5>Limited Liability</h5>
                <p>The liability of the shareholders of a company is limited to the nominal value of the shares held by
                    them. In the event of liquidation, the maximum loss of a shareholder is equal to the nominal value
                    of the shares held by him. The creditors have no claim on the personal assets of the shareholders in
                    the event of a liquidation.</p>
                <h5>Transferability of Shares </h5>
                <p>The shares of a joint-stock company are freely transferable. It does not require any permission from
                    the company or consent of other shareholders. The shares of listed companies can be sold or
                    purchased on the stock exchange and ownership transferred without any difficulty. However, in case
                    of a private limited company, the transfer of shares is subject to the restrictions given in the
                    company’s articles.</p>
                <h5>Ability to Acquire a Broad Capital Base</h5>
                <p>Following are significant factors that enable a company to raise large amount of capital</p>
                <ol>
                    <li>The nominal value of shares is kept small, as a result of which investment of any size is
                        possible.</li>
                    <li>Limited liability minimizes the risk of the investors and makes investment attractive and safer.
                    </li>
                </ol>
                <h5>An Artificial Person created by Law</h5>
                <p>A company is called an artificial person because it does not take birth as a natural person but it
                    comes into existence through the law. The company possesses only those properties which are
                    conferred upon it by its Memorandum of Association (Charter).</p>
                <h5>Continuous Existence</h5>
                <p>The companies generally have a continuous existence irrespective of changes in ownership. In the
                    cases of sole proprietorship and partnership, change in ownership means the dissolution of the
                    original business and formation of a new business.</p>
                <h5>Common Seal</h5>
                <p>Being an artificial person, a company can act through natural persons only. The acts of a company are
                    authorized by the “common seal”. The “common seal” is the official signature of the company. A
                    document not bearing the common seal is not binding on the company.</p>
