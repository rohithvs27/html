                <h2>Sole Proprietorship</h2>
               
                <p>
                    Sole Proprietorship in simple words is a one-man business organisation. Furthermore, a sole
                    proprietor is a natural person(not a legal person/entity) who fully owns and manages this type of
                    entity. In fact, the business and the man are the same, it does not have a separate legal entity.
                </p>
                <p>
                    Sole proprietorship usually does not have to be incorporated or registered. Thus, it is the simplest
                    form of business structure and the ideal choice to run a small business or medium scale business.
                    Let us look at some important features of a proprietorship. </p>

                <h4>Features of Sole Proprietorship</h4>
                <h5>1] Lack of Legal Formalities</h5>
                <p>
                    A sole proprietorship does not have a separate law to govern it. And so there are not many special
                    rules and regulations to follow. Furthermore, it does not require incorporation or registration of
                    any kind. In fact, in most cases, we need only the license to carry out the desired business.</p>
                <p>
                    And just like in its formation, there is hardly any legal process involved in its closure. All in
                    all, it allows for ease of doing business with minimum hassles.
                </p>

                <h5>2] Liability</h5>
                <p>
                    Since there is no separation between the owner and the business, the personal liability of the owner
                    is also unlimited. So if the business is unable to meet its own debts or liabilities, it will fall
                    upon the proprietor to pay them. For instance, he may have to sell all of his personal assets (like
                    his car, house, other properties etc) to meet the debts or liabilities of the business.
                </p>
                <h5>3] Risk and Profit</h5>

                <p>
                    The business owner is the only risk bearer in a sole proprietorship. Since he is the only one
                    financially invested in the company. As a result, he must also bear all the risk. In other words, if
                    the business fails or suffers losses he will be the one affected.
                </p>

                <p>However, he also enjoys all the profits from the business. He does not have to share his profits with
                    any other stakeholders since there are none. So he must bear the full risk in exchange for enjoying
                    full profits.</p>

                <h5>4] No Separate Identity</h5>
                <p>In legal terms, the business and the owner are one and the same. No separate legal identity will be
                    bestowed upon the sole proprietorship. So the owner will be responsible for all the activities and
                    transactions of the business.</p>
                <h5>5] Continuity</h5>
                <p>As seen above the business and the owner has one identity. So a sole proprietorship is entirely
                    dependent on its owner. The death, retirement, bankruptcy. insanity, imprisonment etc will have an
                    effect on the sole proprietorship. In such situations, the proprietorship will cease to exist and
                    the business will come to an end.</p>
                <h4>Advantages of Sole Proprietorship</h4>
                <ul>
                    <li>A proprietor will have complete control of the entire business. Thus this will facilitate quick
                        decisions and freedom to do business</li>
                    <li>Law does not require a proprietorship to publish its financial accounts or any other such
                        documents to any members of the public. As a result, there is enough confidentiality which is
                        important in the business world</li>
                    <li>The business owner derives the maximum incentive from the business. Because he does not have to
                        share any of his profits. So the work he puts into the business is completely reciprocated in
                        incentives</li>
                    <li>Being your own boss is a great sense of satisfaction and achievement. Moreover, you are
                        answerable only to yourself. Hence it is a great boost to your self-worth as well</li>
                </ul>
                <h4>Disadvantages of Sole Proprietorship</h4>
                <ul>
                    <li>One of the biggest limitations of a sole proprietorship is the unlimited personal liability of
                        the owner. If the business fails it can wipe out the personal wealth of the owner as well as
                        affect his future business prospects too</li>
                    <li>Another problem is that a sole proprietor has access to limited capital. The money he can borrow
                        from his own personal savings may not be enough to expand the business. Moreover, banks and
                        financial institutions are also wary of lending to proprietorships.</li>
                    <li>The life cycle of a sole proprietorship is undecided and attached to its owner. An incapacitated
                        owner may have a negative effect on the business, and it may even lead to the closure of the
                        business. A sole proprietorship cannot carry on without its proprietor.</li>
                    <li>A sole proprietor also has limited managerial ability. He cannot be an expert in all the fields
                        of the business. Furthermore, limited resources may mean that he cannot hire competent people to
                        help him out. As a result, the business may suffer from mismanagement and poor decisions.</li>
                </ul>
                <h2>Partnership</h2>
                <p>In India, we have a definite law that covers all aspects and functioning of a partnership, The Indian
                    Partnership Act 1932. The act also defines a partnership as “the relation between two or more
                    persons who have agreed to share the profits from a business carried on by either all of them or any
                    of them on behalf of/acting for all”</p>
                <p>So in such a case two or more (maximum numbers will differ according to the business being carried)
                    persons come together as a unit to achieve some common objective. And the profits earned in pursuit
                    of this objective will be shared amongst themselves.</p>
                <p>The entity is collectively called a “Partnership Firm” and all the individual members are the
                    “Partners”. So let us look at some important features.</p>
                <h4>Features of a Partnership</h4>
                <h5>1] Formation/Contract</h5>
                <p>A partnership firm is not a separate legal entity. But according to the act, a firm must be formed
                    via a legal agreement between all the partners. So a contract must be entered into to form a
                    partnership firm.</p>
                <p>Its business activity must be lawful, and the motive should be one of profit. So two people forming
                    an alliance to carry out charity and/or social work will not constitute a partnership. Similarly, a
                    partnership contract to carry out illegal work, such as smuggling, is void as well.</p>
                <h5>2] Unlimited Liability</h5>
                <p>In a unique feature, all partners have unlimited liability in the business. The partners are all
                    individually and jointly liable for the firm and the payment of all debts. This means that even the
                    personal assets of a partner can be liquidated to meet the debts of the firm.</p>
                <p>If the money is recovered from a single partner, he can, in turn, sue the other partners for their
                    share of the debt as per the contract of the partnership.</p>
                <h5>3] Continuity</h5>
                <p>A partnership cannot carry out in perpetuity. The death or retirement or bankruptcy or insolvency or
                    insanity of a partner will dissolve the partnership. The remaining partners may continue the
                    partnership if they so choose, but a new contract must be drawn up. Also, the partnership of a
                    father cannot be inherited by his son. If all the other partners agree, he can be added on as a new
                    partner.</p>
                <h5>4] Number of Members</h5>
                <p>As we know that there should be a minimum of two members for a partnership. However, the maximum
                    number will vary according to a few conditions. The Partnership Act itself is silent on this issue,
                    but the Companies Act, 2013 provides clarity.</p>
                <p>For a banking business, the number of partners must not exceed ten. For a business of any other
                    nature, the maximum number is twenty. If the number of partners increases it will become an illegal
                    entity or association.</p>
                <h5>5] Mutual Agency</h5>
                <p>In a partnership, the business must be carried out by all the partners together. Or alternatively, it
                    can be carried out by any of the partners (one or several) acting for all of them or on behalf of
                    all of them. So this means every partner is an agent as well as the principal of the partnership.
                </p>
                <p>He represents the other partners in some cases so he is their agent. But in other circumstances, he
                    is bound by the actions of any of the other partners aking him the principal as well.</p>
                <h4>Types of Partners</h4>
                <p>Not all partners of a firm have the same responsibilities and functions. There can be various types
                    of partners in a partnership. Let us study the types of partners and their rights and duties.</p>
                <ul>
                    <li><b> Active Partner: </b> As the name suggests he takes active participation in the business of
                        the firm. He contributes to the capital, has a share in the profit and also participates in the
                        daily activities of the firm. His liability in the firm will be unlimited. And he often will act
                        as an agent for the other partners.</li>
                    <li><b> Dormant Partner: </b> Also known as a sleeping partner, he will not participate in the daily
                        functioning of the business. But he will still have to make his share of contribution to the
                        capital. In return, he will have a share of the profits. His liability will also be unlimited.
                    </li>
                    <li><b> Secret Partner: </b> Here the partner’s association with the firm is not public knowledge.
                        He will not represent the firm to outside agents or parties. Other than this his participation
                        with respect to capital, profits, management, and liability will be the same as all the other
                        partners.</li>
                    <li><b> Nominal Partner: </b> This partner is only a partner in the name. He allows the firm to use
                        the name of his firm, and the attached goodwill. But he in no way contributes to the capital and
                        hence has no share in the profits. He does not involve himself in the firm’s business. But his
                        liability too will be unlimited.</li>
                    <li><b> Partner by Estoppel:</b>If a person makes it out to be, through their conduct or behavior,
                        that they are partners in a firm and he does not correct them, then he becomes a partner by
                        estoppel. However, this partner too will have unlimited liability.</li>
                </ul>
