                <h4>Adjustments to GDP</h4>
                <p>
                    A number of adjustments can be made to a country’s GDP to improve the usefulness of this figure. For
                    economists, a country’s GDP reveals the size of the economy but provides little information about
                    the standard of living in that country. Part of the reason for this is that population size and cost of living  are not
                    consistent around the world. For example, comparing the nominal GDP of China to the nominal GDP of
                    Ireland would not provide much meaningful information about the realities of living in those
                    countries because China has approximately 300 times the population of Ireland.
                </p>
                <p>
                    To help solve this problem, statisticians sometimes compare GDP per capita  between
                    countries. GDP per capita is calculated by dividing a country’s total GDP by its population, and
                    this figure is frequently cited to assess the nation’s standard of living. Even so, the measure is
                    still imperfect. Suppose China has a GDP per capita of $1,500, while Ireland has a GDP per capita of
                    $15,000. This doesn’t necessarily mean that the average Irish person is 10 times better off than the
                    average Chinese person. GDP per capita doesn’t account for how expensive it is to live in a country.
                </p>
                <>
                    Purchasing power parity (PPP) attempts to solve this problem by comparing how many goods and
                    services an exchange-rate-adjusted unit of money can purchase in different countries—comparing the
                    price of an item, or basket of items, in two countries after adjusting for the exchange rate between
                    the two, in effect.
                </>
                <>
                    Real per-capita GDP, adjusted for purchasing power parity, is a heavily refined statistic to measure
                    true income, which is an important element of well-being. An individual in Ireland might make
                    $100,000 a year, while an individual in China might make $50,000 a year. In nominal terms, the
                    worker in Ireland is better off. But if a year’s worth of food, clothing, and other items costs
                    three times as much in Ireland than in China, however, then the worker in China has a higher real income .
                </p>
                <h4> How to Use GDP Data </h4>
                <p>
                    Most nations release GDP data every month and quarter. In the U.S., the Bureau of Economic Analysis
                        (BEA)  publishes an advance release of quarterly GDP four weeks after the quarter ends, and a
                    final release three months after the quarter ends. The BEA releases are exhaustive and contain a
                    wealth of detail, enabling economists and investors to obtain information and insights on various
                    aspects of the economy.
                </p>
                <p>
                    GDP’s market impact is generally limited, since it is “backward-looking,” and a substantial amount
                    of time has already elapsed between the quarter-end and GDP data release. However, GDP data can have
                    an impact on markets if the actual numbers differ considerably from expectations. For example, the
                    S&amp;P 500 had its biggest decline in two months on Nov. 7, 2013, on reports that U.S. GDP
                    increased at a 2.8% annualized rate in Q3, compared with economists’ estimate of approximately
                    2%.<span class=""
                        data-id="#citation-18">7<span
                        class=""
                        data-id="#citation-17">8The data fueled speculation that the stronger
                    economy could lead the U.S. Federal Reserve (the Fed) to scale back its massive stimulus program
                    that was in effect at the time.<span class=""
                        data-id="#citation-11">9
                </p>
                <p>
                    Because GDP provides a direct indication of the health and growth of the economy, businesses can use
                    GDP as a guide to their business strategy. Government entities, such as the Fed in the U.S., use the
                    growth rate and other GDP stats as part of their decision process in determining what type of
                    monetary policies to implement. If the growth rate is slowing, they might implement an expansionary
                    monetary policy to try to boost the economy. If the growth rate is robust, they might use monetary
                    policy to slow things down to try to ward off inflation.
                </p>
                <p>
                    Real GDP is the indicator that says the most about the health of the economy. It is widely followed
                    and discussed by economists, analysts, investors, and policy-makers. The advance release of the
                    latest data will almost always move markets, although that impact can be limited, as noted above.
                </p>
                <h4> GDP and Investing </h4>
                <p>
                    Investors watch GDP since it provides a framework for decision-making. The “corporate profits” and
                    “inventory” data in the GDP report are a great resource for equity investors, as both categories
                    show total growth during the period; corporate profits data also displays pretax profits, operating cash flows , and
                    breakdowns for all major sectors of the economy. Comparing the GDP growth rates of different
                    countries can play a part in asset allocation, aiding decisions about whether to invest in
                    fast-growing economies abroad—and if so, which ones.
                </p>
                <p>
                    One interesting metric that investors can use to get some sense of the valuation of an equity market
                    is the ratio  of total market capitalization
                        to GDP , expressed as a percentage. The closest equivalent to this in terms of stock
                    valuation is a company’s market cap to total sales (or revenues), which in per-share terms is the
                    well-known price-to-sales ratio .
                </p>
                <p>
                    Just as stocks in different sectors trade at widely divergent price-to-sales ratios, different
                    nations trade at market-cap-to-GDP ratios that are literally all over the map. For example,
                    according to the World Bank, the U.S. had a market-cap-to-GDP ratio of 158% for 2019 (the latest
                    year for available figures), while China had a ratio of just over 59% and Hong Kong had a ratio of
                    1,349%.
                </p>
                <p>
                    However, the utility of this ratio lies in comparing it to historical norms for a particular nation.
                    As an example, the U.S. had a market-cap-to-GDP ratio of 141% at the end of 2006, which dropped to
                    78% by the end of 2008. In retrospect, these represented zones of
                    substantial overvaluation and undervaluation, respectively, for U.S. equities.
                </p>
                <p>
                    The biggest downside of this data is its lack of timeliness; investors only get one update per
                    quarter, and revisions can be large enough to significantly alter the percentage change in GDP.
                </p>
                <h4> History of GDP </h4>
                <p>
                    The concept of GDP was first proposed in 1937 in a report to the U.S. Congress in response to the
                    Great Depression, conceived of and presented by an economist at the National Bureau
                        of Economic Research , Simon Kuznets. At the time, the preeminent system of measurement was
                    GNP. After the Bretton
                        Woods  conference in 1944, GDP was widely adopted as the standard means for measuring
                    national economies, although ironically, the U.S. continued to use GNP as its official measure of
                    economic welfare until 1991, after which it switched to GDP.                 </p>
                <p>
                    Beginning in the 1950s, however, some economists and policy-makers began to question GDP. Some
                    observed, for example, a tendency to accept GDP as an absolute indicator of a nation’s failure or
                    success, despite its failure to account for health, happiness, (in)equality, and other constituent
                    factors of public welfare. In other words, these critics drew attention to a distinction between
                    economic progress and social progress. However, most authorities, like Arthur Okun , an economist
                    for President John F. Kennedy’s Council of Economic Advisers, held firm to the belief that GDP is an
                    absolute indicator of economic success, claiming that for every increase in GDP, there would be a
                    corresponding drop in unemployment.
                </p>
                <h4> Criticisms of GDP </h4>
                <p>
                    There are, of course, drawbacks to using GDP as an indicator. In addition to the lack of timeliness,
                    some criticisms of GDP as a measure are:
                </p>
                <ul>
                    <li><strong>It ignores the value of informal or unrecorded economic activity</strong> — GDP relies
                        on recorded transactions and official data, so it does not take into account the extent of
                        informal economic activity. GDP fails to account for the value of under-the-table employment, black market 
                        activity, or unremunerated volunteer work, which can all be significant in some nations and
                        cannot account for the value of leisure time or household production, which are ubiquitous
                        conditions of human life in all societies.</li>
                    <li><strong>It is geographically limited in a globally open economy</strong> —&nbsp;GDP does not
                        take into account profits earned in a nation by overseas companies that are remitted back to
                        foreign investors. This can overstate a country’s actual economic output. For example, Ireland
                        had a GDP of nearly $225 billion 12and GNP of $186.89 billion in 2012 13, the difference of approximately $38 billion
                        (or almost 17% of GDP) largely being due to profit repatriation  by
                        foreign companies based in Ireland.</li>
                    <li><strong>It emphasizes material output without considering overall well-being </strong>— GDP
                        growth alone cannot measure a nation’s development or its citizens’ well-being, as noted above.
                        For instance, a nation may be experiencing rapid GDP growth, but this may impose a significant
                        cost to society in terms of environmental impact and an increase in income disparity.</li>
                       
                    <li><strong>It ignores business-to-business activity</strong> — GDP considers only final goods
                        production and new capital investment and deliberately nets out intermediate spending and
                        transactions between businesses. By doing so, GDP overstates the importance of consumption
                        relative to production in the economy and is less sensitive as an indicator of economic
                        fluctuations compared to metrics that include business-to-business activity.</li>
                    <li><strong>It counts costs and waste as economic benefits</strong> — GDP counts all final private
                        and government spending as additions to income and output for society, regardless of whether
                        they are actually productive or profitable. This means that obviously unproductive or even
                        destructive activities are routinely counted as economic output and contribute to growth in GDP.
                        For example, this includes spending directed toward extracting or transferring wealth between
                        members of society rather than producing wealth (such as the administrative costs of taxation or
                        money spent on lobbying and rent-seeking ); spending on investment projects for which the necessary
                        complementary goods and labor are not available or for which actual consumer demand does not
                        exist (such as the construction of empty ghost cities or bridges to nowhere, unconnected to any
                        road network); and spending on goods and services that are either themselves destructive or only
                        necessary to offset other destructive activities, rather than to create new wealth (such as the
                        production of weapons of war or spending on policing and anti-crime measures).</li>
                 
                </ul>
                <h4> Sources for GDP Data </h4>
                <p>
                    The World Bank  hosts one
                    of the most reliable web-based databases. It has one of the best and most comprehensive lists of
                    countries for which it tracks GDP data. The International Money Fund  (IMF) also provides GDP data through its multiple
                    databases, such as World Economic Outlook and International Financial Statistics.
                </p>
                <p>
                    Another highly reliable source of GDP data is the Organization for Economic
                        Cooperation and Development (OECD) . The OECD not only provides historical data but also
                    forecasts GDP growth. The disadvantage of using the OECD database is that it tracks only OECD member
                    countries and a few nonmember countries.
                </p>
                <p>
                    In the U.S., the Fed collects data from multiple sources, including a country’s statistical agencies
                    and The World Bank. The only drawback to using a Fed database is a lack of updating in GDP data and
                    an absence of data for certain countries.
                </p>
                <p>
                    The Bureau of Economic Analysis
                        (BEA),  a division of the U.S. Department of Commerce ,
                    issues its own analysis document with each GDP release, which is a great investor tool for analyzing
                    figures and trends and reading highlights of the very lengthy full release.
                </p>
                <h4> The Bottom Line </h4>
                <p>
                    In their seminal textbook <em>Economics</em>,&nbsp;Paul Samuelson and William&nbsp;Nordhaus neatly
                    sum up the importance of the national accounts and GDP. They liken the ability of GDP to give an
                    overall picture of the state of the economy to that of a satellite in space that can survey the
                    weather across an entire continent.
                </p>
                <p>
                    GDP enables policy-makers and central banks to judge whether the economy is contracting or
                    expanding, whether it needs a boost or restraint, and if a threat such as a recession or inflation
                    looms on the horizon. Like any measure, GDP has its imperfections. In recent decades, governments
                    have created various nuanced modifications in attempts to increase GDP accuracy and specificity.
                    Means of calculating GDP have also evolved continually since its conception to keep up with evolving
                    measurements of industry activity and the generation and consumption of new, emerging forms of
                    intangible assets.
                </p>
                <h4> Frequently Asked Questions </h4>
                <h3> What is a simple definition of GDP? </h3>
                <p>
                    Gross domestic product
                        (GDP)  is a measurement that seeks to capture a country’s economic output. Countries with
                    larger GDPs will have a greater amount of goods and services generated within them, and will
                    generally have a higher standard of living. For this reason, many citizens and political leaders see
                    GDP growth as an important measure of national success, often referring to “GDP growth” and
                    “economic growth” interchangeably. Due to various limitations, however, many economists have argued
                    that GDP should not be used as a proxy for overall economic success, much less the success of a
                    society more generally.
                </p>
