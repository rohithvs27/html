                <h4>An oveview of audits</h4>
                <p>Audits benefit the programme or project by providing a health check and recommending actions. Senior
                    management gain an insight into the problems in the business, the level of risk they are facing and
                    the actions that need to be taken. The companies’ clients can gain confidence in the quality of the
                    outputs and visibility of any areas that need to be addressed. The responsibility for authorising an
                    audit rests with senior management who should ensure that the audit team have a clear brief of the
                    objectives, scope, timings, restrictions and format. The scope may look at the management, status,
                    quality and/or interfaces of a programme or project. In developing the scope the sponsors may
                    consider size, complexity, results of previous audits, frequency of audits, stakeholder interests
                    and the level of change within the organization.</p>
                <p>Typical audits will either look at operational, structural or product compliance. Operational
                    compliance is concerned with process and methodologies and structural with standards and codes of
                    practice. Product compliance assesses products against specifications and may also check against
                    formal standards like ISO</p>
                <p>Audits may be carried out internally, by the organization’s client perhaps as part of a tender
                    evaluation process, or by an independent organization such as a government, regulatory or
                    accreditation body.
                </p>
