                <span>
                    <h4><span class="contentIntroOne">1. <strong> What is TransUnion CIBIL? What does it
                                do?</strong></span></h4>
                    <p>TransUnion CIBIL Limited is India’s first Credit Information Company, also commonly referred
                        as a Credit Bureau. We collect and maintain records of individuals’ and commercial entities’
                        payments pertaining to loans and credit cards. These records are submitted to us by banks
                        and other lenders on a monthly basis; using this information a CIBIL Score and Report for
                        individuals is developed, which enables lenders to evaluate and approve loan applications.
                    </p>
                    <p>A Credit Bureau is licensed by the RBI and governed by the Credit Information Companies
                        (Regulation) Act of 2005.</p>

                    <h4><span class="contentIntroOne">2. Wh<strong>y is my CIBIL Score important for getting my loan
                                sanctioned?</strong></span></h4>
                    <p>The CIBIL Score plays a critical role in the loan application process. After an applicant
                        fills out the application form and hands it over to the lender, the lender first checks the
                        CIBIL Score and Report of the applicant. If the CIBIL Score is low, the lender may not even
                        consider the application further and reject it at that point. If the CIBIL Score is high,
                        the lender will look into the application and consider other details to determine if the
                        applicant is credit-worthy. The CIBIL Score works as a first impression for the lender, the
                        higher the score, the better are your chances of the loan being reviewed and approved. The
                        decision to lend is solely dependent on the lender and CIBIL does not in any manner decide
                        if the loan/credit card should be sanctioned or not.</p>

                    <h4><span class="contentIntroOne">3. What is a CIBIL Score and what factors affect my CIBIL
                            Score?</span></h4>
                    <p>CIBIL Score is a 3 digit numeric summary of your credit history, derived by using details
                        found in the ‘Accounts’ and ‘Enquiries’ sections on your CIBIL Report and ranges from 300 to
                        900. The closer your score is to 900, the higher are the chances of your loan application
                        getting approved.</p>
                    <p><img src="assets/img/blog/cibil-bnr-1.jpg" alt="4 major factors"></p>
                    <h4><span class="contentIntroOne">4. How can I improve my CIBIL Score?<br></span></h4>
                    <p>You can improve your CIBIL Score by maintaining a good credit history, which is essential for
                        loan approvals by lenders. Follow these 6 steps which will help you better your score:</p>
                    <ul>
                        <li><strong>Always pay your dues on time: </strong>Late payments are viewed negatively by
                            lenders</li>
                        <li><strong>Keep your balances low</strong>: Always be prudent to not use too much credit,
                            control your utilization.</li>
                        <li><strong>Maintain a healthy mix of credit:&nbsp;</strong>It is better to have a healthy
                            mix of secured (such as home loan, auto loan) and unsecured loans (such as personal
                            loan, credit cards). Too many unsecured loans may be viewed negatively.</li>
                        <li><strong>Apply for new credit in moderation:&nbsp;</strong>You don’t want to reflect that
                            you are continuously seeking excessive credit; apply for new credit cautiously.</li>
                        <li><strong>Monitor your co-signed, guaranteed and joint accounts monthly: </strong>In
                            co-signed, guaranteed or jointly held accounts, you are held equally liable for missed
                            payments. Your joint holder’s (or the guaranteed individual) negligence could affect
                            your ability to access credit when you need it.<strong><br></strong></li>
                        <li><strong>Review your credit history frequently throughout the year:&nbsp;</strong>Monitor
                            your CIBIL Score and Report regularly to avoid unpleasant surprises in the form of a
                            rejected loan application.</li>
                    </ul>
                    <h4><span class="contentIntroOne">5. Can CIBIL delete or change my records?</span>
                        <p>
                            CIBIL cannot delete or change records reflecting on your CIR on its own; we simply
                            collect records of individuals provided to us by our members (Banks and financial
                            institutions). There are no ‘good’ and ‘bad’ credit or defaulters lists either.
                    </h4>
                    </p>
                    <h4><br><span class="contentIntroOne">6.&nbsp;<strong>What does it mean when my Score is "NA" or
                                "NH"?</strong></span></h4>
                    <p>A Score of “NA” or “NH” is not a bad thing at all. These mean one of the below:</p>
                    <ul>
                        <li>You do not have a credit history or you do not have enough of a credit history to be
                            scored, i.e. you are new to the credit system</li>
                        <li>You do not have any credit activity in the last couple of years</li>
                        <li>You have all add-on credit cards and have no credit exposure</li>
                    </ul>
                    <p>It is important to note that while these Scores are not viewed negatively by a lender, some
                        lenders’ credit policy prevents them from providing loans to an applicant with Scores of
                        “NA” or “NH” (applicants with no credit track record). Hence, you may have better chances
                        applying for a loan elsewhere.</p>
                    <h4><br><span class="contentIntroOne">7. What is CIBIL Score 2.0?</span></h4>
                    <p>The CIBIL Score 2.0 is a new, updated version of CIBIL Score which has been designed keeping
                        in mind the current trends and changes in the consumer profiles &amp; credit data. Banks are
                        gradually switching to the new version and you may find a difference in the new version when
                        compared to the earlier version (i.e., the score 2.0 may be lower than the earlier
                        version).&nbsp;Please note, the score displayed on the dashboard is the earlier version.
                        However, the difference in the Credit Score does not impact the credit decisioning during
                        the Loan approval process as both the versions of the score may have a different score
                        eligibility cut off while processing the loan application.&nbsp;Lenders may have a different
                        loan eligibility criteria depending on the version they are using.</p>
                    <p>The CIBIL Score 2.0 also introduces a risk index score range for those individuals who have a
                        credit history of less than 6 months. These individuals were categorized under the category
                        of “No History – NH” in the earlier version. The score range is from 1 – 5, with 1
                        signifying “high risk” and 5 signifying “low risk”.</p>
