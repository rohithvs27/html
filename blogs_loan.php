                <h2> What Is a Loan? </h2>
                <p> The term loan refers to a type of credit vehicle in which a sum of money is lent to another party in
                    exchange for future repayment of the value or principal amount. In many cases, the lender also adds
                    interest and/or finance charges to the principal value which the borrower must repay in addition to
                    the principal balance. Loans may be for a specific, one-time amount, or they may be available as an
                    open-ended line of credit up to a specified limit. Loans come in many different forms including
                    secured, unsecured, commercial, and personal loans. </p>


