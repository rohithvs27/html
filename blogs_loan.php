                <h4> Types of Loans </h4>
                <p> Loans come in many different forms. There are a number of factors that can differentiate the costs
                    associated with them along with their contractual terms. </p>
                <h5> Secured vs. Unsecured Loan </h5>
                <p> Loans can be secured or unsecured. Mortgages and car loans are secured loans, as they are both
                    backed or secured by collateral. In these cases, the collateral is the asset for which the loan is
                    taken out, so the collateral for a mortgage is the home, while the vehicle secures a car loan.
                    Borrowers may be required to put up other forms of collateral for other types of secured loans if
                    required. </p>
                <p> Credit cards and signature loans are unsecured loans. This means they are not backed by any
                    collateral. Unsecured loans usually have higher interest rates than secured loans because the risk
                    of default is higher than secured loans. That's because the lender of a secured loan can repossess
                    the collateral if the borrower defaults. Rates tend to vary wildly on unsecured loans depending on
                    multiple factors including the borrower's credit history. </p>
                <h5> Revolving vs. Term Loan </h5>
                <p> Loans can also be described as revolving or term. A revolving loan can be spent, repaid, and spent
                    again, while a term loan refers to a loan paid off in equal monthly installments over a set period.
                    A credit card is an unsecured, revolving loan, while a home equity line of credit (HELOC) is a
                    secured, revolving loan. In contrast, a car loan is a secured, term loan, and a signature loan is an
                    unsecured, term loan. </p>
