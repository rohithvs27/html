                <h4> Understanding Loans </h4>
                <p> A loan is a form of debt incurred by an individual or other entity. The lender—usually a
                    corporation, financial institution, or government—advances a sum of money to the borrower. In
                    return, the borrower agrees to a certain set of terms including any finance charges, interest,
                    repayment date, and other conditions. In some cases, the lender may require collateral to secure the
                    loan and ensure repayment. Loans may also take the form of bonds and certificates of deposit (CDs).
                    It is also possible to take a loan from a 401(k) account. </p>
                <p> Here's how the loan process works. When someone needs money, they apply for a loan from a bank,
                    corporation, government, or other entity. The borrower may be required to provide specific details
                    such as the reason for the loan, their financial history, Social Security Number (SSN), and other
                    information. The lender reviews the information including a person's debt-to-income (DTI) ratio to
                    see if the loan can be paid back. Based on the applicant's creditworthiness, the lender either
                    denies or approves the application. The lender must provide a reason should the loan application be
                    denied. If the application is approved, both parties sign a contract that outlines the details of
                    the agreement. The lender advances the proceeds of the loan, after which the borrower must repay the
                    amount including any additional charges such as interest. </p>
                <p> The terms of a loan are agreed to by each party before any money or property changes hands or is
                    disbursed. If the lender requires collateral, the lender outlines this in the loan documents. Most
                    loans also have provisions regarding the maximum amount of interest, as well as other covenants such
                    as the length of time before repayment is required. </p>
                <p> Loans are advanced for a number of reasons including major purchases, investing, renovations, debt
                    consolidation, and business ventures. Loans also help existing companies expand their operations.
                    Loans allow for growth in the overall money supply in an economy and open up competition by lending
                    to new businesses. The interest and fees from loans are a primary source of revenue for many banks,
                    as well as some retailers through the use of credit facilities and credit cards. </p>
                <h4> Special Considerations </h4>
                <p> Interest rates have a significant effect on loans and the ultimate cost to the borrower. Loans with
                    higher interest rates have higher monthly payments—or take longer to pay off—than loans with lower
                    interest rates. For example, if a person borrows $5,000 on a five-year installment or term loan with
                    a 4.5% interest rate, they face a monthly payment of $93.22 for the following five years. In
                    contrast, if the interest rate is 9%, the payments climb to $103.79. </p>
                <p> Similarly, if a person owes $10,000 on a credit card with a 6% interest rate and they pay $200 each
                    month, it will take them 58 months, or nearly five years, to pay off the balance. With a 20%
                    interest rate, the same balance, and the same $200 monthly payments, it will take 108 months, or
                    nine years, to pay off the card. </p>
                <h5> Simple vs. Compound Interest </h5>
                <p> The interest rate on loans can be set at simple or compound interest. Simple interest is interest on
                    the principal loan. Banks almost never charge borrowers simple interest. For example, let's say an
                    individual takes out a $300,000 mortgage from the bank, and the loan agreement stipulates that the
                    interest rate on the loan is 15% annually. As a result, the borrower will have to pay the bank a
                    total of $345,000 or $300,000 x 1.15. </p>
                <p> Compound interest is interest on interest and means more money in interest has to be paid by the
                    borrower. The interest is not only applied to the principal but also the accumulated interest of
                    previous periods. The bank assumes that at the end of the first year, the borrower owes it the
                    principal plus interest for that year. At the end of the second year, the borrower owes it the
                    principal and the interest for the first year plus the interest on interest for the first year. </p>
                <p> With compounding, the interest owed is higher than that of the simple interest method because
                    interest is charged monthly on the principal loan amount, including accrued interest from the
                    previous months. For shorter time frames, the calculation of interest is similar for both methods.
                    As the lending time increases, the disparity between the two types of interest calculations grows.
                </p>
