<p><strong>26AS </strong>is a <strong>tax credit statement </strong>and covers all the amounts of TDS deducted by others. This might happen that someone has deducted your tax but the same isn’t appearing in your tax credit statements, which may be simply due to non-filing of TDS return by the deductor. In such cases, please make sure to obtain a TDS certificate as this will be an ultimate proof that your tax has been deducted at source.</p>
<h4>How to apply for TDS refund</h4>
<p>There is this major misconception that refund of excess TDS is different from income tax refund and is called as TDS refund. However, the fact is that there is only one kind of return which you claim while filing your annual income tax return. Nowadays, it is compulsory to quote bank account details such as account number and IFSC code while filing of return and non-entering of such details will not generate a valid .xml file. In case if someone has deducted more tax than he should have deducted, then income tax refund will arise which can be claimed upon the filing of your annual income tax return.</p>
<p>For example, you own a goods transport agency and yours is a proprietorship firm. You presented an invoice of Rs 50,000/- and the person paying freight paid you a net amount of Rs 49,000/- (after deducting tax of Rs 1,000/- @ 2% under section 194C). In this case, the deductor deducted tax @ 2% instead of 1% and hence deducted excess TDS by Rs 500/-. This excess TDS will arise as a refund in the income tax return.</p>
<h4>Applicability of TDS on transactions of immovable property.</h4>
<p>There are mainly two sections that prescribe for deduction of taxes on transactions related to an immovable property:</p>
<p>1. Section 194-I: Section 194-I requires for deduction of tax at source on rental income @ 10% for rent on land &amp; building if the total amount of rent paid/credited or to be paid/to be credited exceeds the cap of Rs 1,80,000/- during a financial year. Please note that individuals and HUFs who are not subject to tax audits under section 44AB need not deduct tax at source on such rental expenses.</p>
<p>2. Section 194IA: Section 194IA came into effect from June 2013 which required deduction of tax by the transferee before making payment to transferor @ 1% of the consideration for immovable property. Any sum paid by way of consideration for the transfer of any immovable property (other than agricultural land) is covered under section 194-IA, provided the consideration for the transfer of an immovable property is not less than Rs. 50 lakhs.</p>
<p>3. Section 194LA: Section 194LA provides for deduction of tax at source @ 10% for the payment to be made to the assessee as a compensation on account of compulsory acquisition of immovable property. Please note that no deduction shall be made under this section where the amount of such payment or, as the case may be, the aggregate amount of such payments to a resident during the financial year does not exceed Rs 250000/-.</p>
