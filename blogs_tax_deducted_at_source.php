            <h4>Introduction</h4>
            <p>
            The concept of TDS was introduced with an aim to collect tax from the very source of income. As per this concept, a person (deductor) who is liable to make payment of specified nature to any other person (deductee) shall deduct tax at source and remit the same into the account of the Central Government. The deductee from whose income tax has been deducted at source would be entitled to get credit of the amount so deducted on the basis of <b>Form 26AS</b> or TDS certificate issued by the deductor.
            </p>
            <p>
            <b>TAX DEDUCTED AT SOURCE (TDS)</b> is a system introduced by Income Tax Department, where person responsible for making specified payments such as salary, commission, professional fees, interest, rent, etc. is liable to deduct a certain percentage of tax before making payment in full to the receiver of the payment. As the name suggests, the concept of TDS is to deduct tax at its source.
            </p>
            <p>
            TDS helps in reducing tax filing burdens for a deductee and ensures stable revenue for the government.
            </p>
           
            <h4>Advantages of TDS:</h4>
            <p>
            TDS is based on the principle of ‘pay as and when you earn’. TDS is a win-win scenario for both the taxpayers and the government. Tax is deducted when making payments through cash, credit or cheque, which is then deposited with the central agencies
            </p>
            <p><b>
            Let us take an example of TDS assuming the nature of payment is professional fees on which specified rate is 10%.
            </b></p>

            <p>
            Uflex Ltd makes a payment of Rs 60,000/- towards professional fees to Mr. Shyam Agarwal (Chartered Accountant), then Uflex Ltd shall deduct a tax of Rs 6,000/- and make a net payment of Rs 54,000/- (60,000/- deducted by Rs 6,000/-) to Shyam Agarwal. The amount of 6,000/- deducted by Uflex Ltd will be directly deposited by Uflex Ltd to the credit of the government.
            </p>

             <p><strong>Uflex Ltd </strong>makes a payment of Rs 60,000/- towards professional fees to <strong>Mr. Shyam Agarwal (Chartered Accountant)</strong>, then <strong>Uflex Ltd </strong>shall deduct a tax of Rs 6,000/- and make a net payment of Rs 54,000/- (60,000/- deducted by Rs 6,000/-) to <strong>Shyam Agarwal</strong>. The amount of 6,000/- deducted by <strong>Uflex Ltd </strong>will be directly deposited by <strong>Uflex Ltd </strong>to the credit of the government.</p>
       
        <h4>General terms of TDS Concept</h4>
        <p><strong>What Is TAN and How to apply for TAN?</strong></p>
        <p>TAN stands for Tax Deduction Account Number. It is an unique identification number which is 10 digit alpha numeric number required to be obtained by all persons who are responsible for deducting or collecting tax. Under Section 203A of the Income Tax Act, 1961, it is mandatory to quote Tax Deduction Account Number (TAN) allotted by the Income Tax Department (ITD) on all TDS returns. The procedure for application of TAN is very simple and can be done online by filling up Form 49B.</p>
        
        <h4>What is TDS Certificate?</h4>
        <p>TDS certificates are issued by the deductor (the person who is deducting tax) to the deductee (the person from whose payment the tax is deducted). There are mainly two types of TDS certificates issued by the deductor.</p>
        <p>1. <u>Form 16</u>: which is issued by the employer to the employee incorporating details of tax deducted by the employer throughout the year, and</p>
        <p>2. <u>Form 16A</u>: which is issued in all cases other than salary.</p>
        
        <p>Your certificate will have total validation, unless the officer cancels it.</p>
        <p>As TDS is collected on an ongoing basis, it can be difficult to keep track of deductions by an individual. As per Section 203 of the ITA, the deductor has to furnish a certificate of TDS payment to the deductee/payee. This certificate is also offered by banks making deductions on pension payments etc. The certificate is typically issued at the deductor’s own letterhead. Individuals are advised to request for TDS certificate wherever applicable, and if not already provided.</p>
        <p>For example, Mr. Gupta is working as a salaried employee at a company and tax is deducted on his salary @ 15%. The company shall provide Mr. Gupta with a Form 16 describing particulars in detail regarding the amount of salary paid and tax deducted on the same.</p>
        <p>However, had Mr. Gupta been working as a professional and received professional fees from an organization which is subject to TDS, then he will be provided Form 16A for the same.</p>
        
        <h4>When TDS should be deducted?</h4>
        <p>The concept of TDS is based on a simple principle i.e. tax is to be deducted <strong>at the time of payment getting due or actual payment whichever is earlier</strong>. A set of scenarios for will be helpful in understanding the concept:</p>
        <p>Say, ABC Private Limited has to make payment of Rs 50,000/- to Mr. XYZ in exchange of professional services.</p>
       
        <h4>Scenario</h4>
        <p>1: Mr. XYZ was paid Rs 30,000/- in advance on 15<sup>th</sup> July. XYZ raised invoice after completion of work on 31<sup>st</sup> July and rest of payment is to be made.</p>
        <p>In such case the company should have deducted tax in the following manner: <u>On 15</u><sup>th</sup><u> July</u>: Rs 3000/- (@ 10% on advance of Rs 30000/-)</p>
        <p><u>On 31</u><sup>st</sup><u> July:</u> Rs 2000/- (@ 10% of total invoice amount as deducted by tax already deducted i.e. Rs 5000/- deducted by Rs 3000/-)</p>
        <h4>Scenario</h4>
        <p>2: Mr XYZ raised the invoice on 15<sup>th</sup> July and was paid whole consideration at one go on 31<sup>st</sup> July.</p>
        <p>In such whole amount of Rs 5000/- shall be deducted on 15<sup>th</sup> July, the date when payment got due, and a net payment of Rs 45000/- shall be made on 31<sup>st</sup> July.</p>
        <h4>Scenario</h4>
        <p>3. Mr. XYZ is to receive the whole amount of Rs 50,000/- well in advance before completion of the assignment.</p>
        <p>In such particular case tax of Rs 5000/- shall be deducted right at the time of payment of advance and no tax is to be deducted at the time of making an entry for the bill due.</p>
        
        <h4>Can I request tax deductors to not subtract tax from an amount and pay the whole amount to me?</h4>
        <p>Non-deduction of tax at source is possible only if your income is going to be below the minimum income tax slab. If that is the case with you, then you can declare your income as being <strong>lower than Rs. 2.5 lakh </strong>(or others as applicable to various category of citizens) through Form 15G/15H and provide the form to the deductor. <strong>Form 15G is for individuals </strong>and Form <strong>15H for senior citizens</strong>. You can also apply to the Assessing Officer of the Income Tax Department through <strong>Form 13 and get a certificate approving deduction of lower taxes or nil deduction of taxes</strong>. But if your income is above the minimum tax rate slab, then you cannot seek exemption from TDS.</p>
        <p>One major difference between <strong>Form 13 and Form 15G/15H is Form 15G/15H </strong>can be issued only by individuals assesses, whereas request in Form 13 can be submitted by any person i.e. individual, partnership firm, company, etc. to the ASSESSING OFFICER to get approval for deduction of taxes at lower or NIL rate.</p>
        
        <h4>How much tax should be deducted from salary?</h4>
        <p>Persons responsible for paying salary are liable to deduct tax on estimated salary at prescribed rate of 15% subject to following:</p>
        <p>1. Exemption Limit: No tax is required to be deducted at source unless the estimated salary exceeds basic exemption limit.</p>
        <p>2. Exempt allowances: Allowances such as LTC, HRA, conveyance, travelling exempt as per prescribed limits and other perquisites not forming part of salary should be deducted from total salary while calculating taxable salary.</p>
        <p>3. Other deductions: Other deductions such as deductions under section 80C, 80CCC, 80CCD, 80CCG, 80D, 80DD, 80DDB, 80E, 80EE, etc. should be considered before the calculation of tax on salary.</p>
        
        <h4>Minimum salary one should have for TDS to be deducted by the employer.</h4>
        <p>If after comprehensive calculation of allowable allowances, taxable perquisites and deductions under chapter VI-A, income from salary head exceeds a sum of basic exemption limit, then tax has to be deducted by the employer @ 15% on the amount over and above the basic exemption limit. For example, the salary of Mr. A arrives at Rs 2,80,000/- assuming that all the allowances, perquisites, and deductions have been taken into consideration, tax @ 15% on Rs 30000/- (2,80,000 – 2,50,000) shall be deducted by the employer.</p>
        <p>Salary needs to be subject to TDS only if the employee falls under the Income Tax Slab. This means that an individual earning less than Rs. 2.5 lakh, senior citizens with a salary of less than Rs. 3 lakh and super seniors (above the age of 80) earning less than Rs. 5 lakh, do not need to pay tax and hence no TDS has to be deducted from their remuneration.</p>
        <p>Hence, provisions of TDS shall attract only if minimum salary is above the basic exemption limit.</p>
        <p>TDS on income from salaries. are deducted on an estimation made at the start of the financial year. The employer is responsible for deducting taxes every month in equal instalments. In case the deductee has switched jobs during the fiscal year, the employer will deduct taxes on the basis of all accrued income in the fiscal year. Deductees should be very careful when mentioning their overall income as tax avoidance will be penalised by relevant authorities</p>
