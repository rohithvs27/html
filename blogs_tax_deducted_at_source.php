<h4>Penalty provisions for non-deduction of TDS.</h4>
<p>There are several instances where interest, fees, and penalty are levied on non-compliance of TDS provisions. The same are discussed here step by step:</p>
<p><strong>1. <u>Consequences of non-deduction of TDS</u></strong></p>
<p>If a person who was responsible for deducting tax at source fails to do so, then the ASSESSING OFFICER has powers to disallow whole of such expenditure for ascertaining taxable profits. For example, ABC Limited paid a commission of Rs 2,00,000/- during the year to a single person and omitted to deduct tax on the same, then the Assessing Officer has powers to disallow deduction whole of such expenses while ascertaining taxable profits.</p>
<p><strong>2. <u>Late deduction of TDS</u></strong></p>
<p>Tax is to be deducted at the time of payment/credit getting due or payment whichever is earlier. In the terms of income tax, even a single day is counted as a month for the purpose of calculating interest. In cases of late deduction of tax, interest @ 1% per month of the TDS amount subject to maximum amount of TDS is levied.</p>
<p>For example, ABC company was supposed to deduct tax of Rs 20000/- on 15<sup>th</sup> July but instead the same was deducted by the company on 1<sup>st</sup> August. In this case interest of Rs 200/- (@1% for one month) is required to be paid by the assesse.</p>
<p><strong>3. <u>Late payment of TDS</u></strong></p>
<p>Tax is to be deducted and paid to the credit of government on every 7<sup>th</sup> day of the succeeding month in which the tax has been deducted; otherwise, interest @ 1.5% per month of TDS amount subject to maximum amount of TDS is levied. For example, ABC Ltd was supposed to deposit TDS of Rs 20000/- deducted in the month of April by 7<sup>th</sup> of May but fails to deposit the same on time and actually deposited the same in the following month. In this case interest of Rs. 300/- (@ 1.5% for one month) is required to paid by the assessee.</p>
<p><strong>4. <u>Late filing of return of TDS</u></strong></p>
<p>TDS returns are required to be filed in the last month of following quarter i.e. 31<sup>st</sup> July, 31<sup>st</sup> October, 31<sup>st</sup> January and in the case of March it is 31<sup>st</sup> May. Fees under section 234E are levied @ <strong>Rs 200/- per day </strong>subject to maximum amount of TDS until the return is filed.</p>
<p>Example, M/s ABC, a partnership deducted and paid a total TDS of Rs 40000/- in the first quarter of FY and was supposed to file its TDS return by 31<sup>st</sup> July but filed its return on 31<sup>st</sup> August. Total fees of Rs 6200 (200/- per day for 31 says) shall be paid before filing of return.</p>
<p><strong>5. <u>Penalty for late filing of TDS return</u></strong></p>
<p>Assessing officer may direct a person who fails to file the statement of TDS within due date to pay penalty minimum of Rs. 10,000 which may extend to Rs.1,00,000. The penalty under this section is in addition to the penalty u/s 234E and also cover the cases of incorrect filing of TDS return.</p>
