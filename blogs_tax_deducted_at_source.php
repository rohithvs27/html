        <p>* The rate of TDS shall be increased by applicable surcharge and Health &amp; Education cess. <strong>How to calculate TDS?</strong></p>
        <p>Numerous transactions are covered under the purview of TDS sections and calculation of TDS can be tricky in some sections. Here, we shall discuss some examples of different sections to make the calculation clear.</p>
       <h4>Example</h4>
        <p><strong>1: </strong>Under the section, 194A tax is to be deducted on payment of interest other than interest on securities. However, no tax is required to be deducted if amount of such interest paid or credited or is likely to be paid or credited does not exceed Rs 10,000/- in case of banking company, co-operative society engaged in the business of banking and post office deposits and Rs 5,000/- in any other case in a financial year. Also, note that no tax is to be deducted on savings account interest.</p>
        <ul>
        <li><u>Scenario 1:</u> Suppose interest paid or credited or is likely to be paid or credited by a banking company to a person in a financial year is Rs 9,000/-, then no tax is required to be deducted as the amount has not exceeded the cap of Rs 10,000/-.</li>
        <li><u>Scenario 2:</u> Say interest paid or credited or is likely to be paid or credited by a banking company to a person in a financial year is Rs 12,000/-, then tax is required to be deducted on the whole amount of Rs 12,000/- @ 10% i.e. TDS of Rs 1200/-.</li>
        </ul>
        <p><strong>Please note that Rs 10,000/- is a cap just for fixing responsibility of banking company for TDS and is not an exemption limit i.e. tax is to be deducted from the whole amount of Rs 12,000/- as soon as the amount exceeds the cap amount of 10,000/-</strong></p>
        <p>Similar examples are relevant for other interest, except in those cases the cap amount shall be Rs 5,000/- instead of Rs 10,000/-.</p>
       <h4>Example</h4>
        <p><strong>2: </strong>Under the section, 194C tax is to be deducted on payment or credit to a resident contractor/sub-contractor. The definition of a contract is derived from the Indian Contract Act, 1872 and covers almost all type of contracts under its purview. However, no tax is to be deducted where:</p>
        <ul>
        <li>the sum is credited or paid in pursuance of any contract, the consideration for which does not exceed Rs. 30,000/-, or,</li>
        <li>where the aggregate of the amounts of such sums credited or paid or likely to be credited or paid during the financial year does not exceed 75,000/-</li>
        </ul>
        <p>Applicable @ 1% if payment/credit is made to resident individual or HUF, @ 2% if payment/credit is made to any resident person other than individual / HUF and @ 20% is PAN is not available.</p>
        <p><u>Scenario 1:</u> Mr. A, an individual provided contractual services to a firm and was made payments in 3 installment, 1<sup>st</sup> installment of Rs 25,000/- and the second installment of Rs 26,000/- and last installment of Rs 28,000/-.</p>
        <p>In this case, the firm need not deduct tax on installments since the amount hasn’t exceeded the cap of Rs 30,000/-. But, if we sum up all 3 installments the total arrives at Rs 79000/-which exceeds the yearly cap of Rs 75,000/-. Hence, in this case, the tax is to be deducted from the whole amount of Rs 75,000/- @ 1% (being an individual), which arrives at Rs 750/-.</p>
        <p><strong>Please note that once the total amount exceeds Rs 75000/- in a financial year, the tax is to be deducted from each and every payment irrespective of the fact whether such part payments are more or less than Rs 30,000/-.</strong></p>
        <p><u>Scenario 2:</u> M/s ABC, a partnership firm provided some contractual services to Mr. A and was made payments in 3 installments of Rs 50,000/-, Rs 12,000/- and Rs 14,000/-.</p>
        <p>In this case, tax @ 2% (being a partnership firm) shall be deducted at the time of payment of Rs 50,000/- as the sum exceeds the cap of a single payment of Rs 30,000/-.</p>
        <p>No tax shall be deducted when the sum of Rs 12,000/- is paid as the sum is far below the cap of a single payment of Rs 30,000/- and the total payment during hasn’t exceeded the yearly cap of Rs 75,000/-.</p>
        <p>Tax @ 2% shall be deducted from the whole amount of Rs 12000/- and Rs 14000/- as they might not have exceeded the cap of single payments, but the yearly cap of Rs 75000/- is exceeded as and when the final installment of Rs 14000/- is paid to M/s ABC.</p>
        <h4>Due dates for TDS:-</h4>
        <p>Payment of TDS each month and filing of quarterly return of TDS are 2 separate processes and due dates for these processes are different</p>
        <p>The due dates for the payment of the deducted TDS are on or before 7th of next month and for the month of March due date is on or before 30<sup>th</sup> April. It means, if the deductor has deducted tax from payments in month of November, then he has to pay the TDS on or before 7th of December.</p>
