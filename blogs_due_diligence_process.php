                <h4><strong><u>Due Diligence: An exercise</u></strong></h4>
                <p>In its simplest definition, means process of investigation, verification and examination of various
                    Legal as well as financial database a Company holds which includes a vast check on its history with
                    regard to financial performance, its assets and obligations and the reputation it holds in the
                    market. Before making any investment the potential buyer needs to step into the shoes of the
                    potential seller and make a thorough check on the key determinants that would be able to drive the
                    Company. For the sake of determining various benefits that could be derived through the deal and
                    investigating into the shortcomings, a panel of due diligence experts needs&nbsp;to be&nbsp;engaged
                    for carrying out various financial and legal checks on the Target Company. The key step involved in
                    due diligence is&nbsp;the valuation of&nbsp;business model of the Company taking into account its
                    various financial database and performance.&nbsp;Have a business valuation performed
                    to&nbsp;determine&nbsp;how much the business is worth, and consider how the current owner’s
                    connections and&nbsp;expertise&nbsp;may affect that value. In a business-to-business&nbsp;company, a
                    business sale could cause the former owner’s clients to leave, which would seriously impact the
                    value of the business. Not only the financial but also the compliance part of various legislations
                    needs&nbsp;to be&nbsp;taken into account.&nbsp;&nbsp;For e.g, the pending litigations against the
                    Company, non-compliance of various laws applicable to the Company varying from 
                  Companies Ac</strong>t,
                    to&nbsp;MRTP&nbsp;Act,&nbsp;SEBI&nbsp;Regulations and what not.</p>
                <h4>
                    <strong><u>Legal Due Diligence:</u></strong>                    
                </h4>
                <p>It is an exercise to&nbsp;review&nbsp;all the legal and statutory documents that a Company holds,
                    to&nbsp;overview&nbsp;compliance with all the laws that are applicable to the Company, whether on
                    general basis or industry specific basis. It would be this due diligence, combined with financial
                    due diligence that would derive the&nbsp;actual&nbsp;valuation of the business and would help in
                    landing up a fair price value. The backing sentiment behind a legal due diligence is to seek and
                    understand all the potential risks to which the Company&nbsp;is exposed&nbsp;as the buyer would
                    always want to know the risks and the ways in which it could be met and adhered.</p>
                <h4><strong><u>Objectives:</u></strong></h4>
                <p>Performing an expert legal due diligence saves costs to the buyer&nbsp;and&nbsp;enriches them with
                    potential information on the threats and risks involved and well as
                    conducting&nbsp;SWOT&nbsp;analysis that would drive the investment and&nbsp;buy-in. It involves
                    going through the documents from the scratch and&nbsp;ascertain&nbsp;any non-compliances with
                    regards to applicable laws and carry on investigation of the obligations of the Company including
                    its debts, leases, pending lawsuits, employee agreements, vendor agreements, security interest
                    created on their assets and other disputed property and papers. Generally specifying it includes the
                    following:</p>
                <p>Ø&nbsp;Assessing and quantifying the main legal risks</p>
                <p>Ø&nbsp;Provide solution to the risk’s so assessed and devising measures to mitigate it through
                    various measures.</p>
                <p>Ø&nbsp;Get to the bottom of all the legal contracts ever entered into and decoding it
                    to&nbsp;ascertain&nbsp;any liabilities arising out of the same.</p>
                <p>Ø&nbsp;For a Company that&nbsp;is listed,&nbsp;it should comply with&nbsp;SEBI&nbsp;Regulations.</p>
                <p>Ø&nbsp;Compliance related to the industry specific laws such as Aviation laws, Food Laws, Banking
                    Regulations and so on.</p>
                <h4><strong><u>Key aspects to consider:</u></strong></h4>
                <p>Before getting into the intricacies of legal due-diligence one must create a comprehensive checklist
                    that takes into account various aspects of key fundamentals on which the corporate structure relies.
                    This could be either&nbsp;the organizational management of a specific Company or its by-laws that
                    forms a key ingredient in&nbsp;day-to-day&nbsp;running of the business activities. Some of such
                    aspects and documents that needs&nbsp;assessment in both letter and spirit includes:</p>
                <h4><strong><u>Organizational Structure</u></strong></h4>
                <ol style="text-align: justify;">
                    <li>Organizational Chart and management philosophy engaged in running the business.</li>
                    <li>Corporate by laws, which could be also held up in the Articles of Association of the Company,
                        which again is a key ingredient in running and optimizing the Company and&nbsp;has&nbsp;the set
                        of rules which needs&nbsp;to be&nbsp;adhered to in day to day operations of the Company.</li>
                    <li>The stakeholders, not to mention are the key people who&nbsp;reflect&nbsp;the overall growth and
                        performance of the Company. A key study of stakeholders mainly the Angel and institutional
                        investors would speak a lot about the Company’s efficiency and the way it delivers to multiply
                        their investments.</li>
                    <li>Minutes are the secret treasure to any Company’s past activity. They&nbsp;reflect&nbsp;the
                        decisions taken by the Company in the past and are the key records for any existing value plan
                        and activity. The minutes include the resolutions passed by the Company in its Board meetings,
                        Shareholders meeting and the decisions made&nbsp;therein. Therefore, a thorough reading and
                        understanding of the minutes is needed to synchronize the acts done by the Company in the past
                        and its results.</li>
                    <li>Conduct discussions with various committees formed in the Company, may it be Risk Management
                        committee, Audit committee, Independent Director’s committee and so on. It also gives a true and
                        clear picture about the trends being followed in a particular organization and the basic
                        structure that&nbsp;is relied&nbsp;upon for conducting various compliances and management tasks.
                    </li>
                </ol>
                <h4><strong>Contractual Obligations</strong></h4>
                <p>The most&nbsp;time-consuming&nbsp;and critical part of any due diligence exercise is the study
                    undertaken to&nbsp;review&nbsp;the various contracts and arrangements undertaken and entered into
                    between the corporates with other body corporates or with its own suppliers, employees/workmen, and
                    other third parties. These contracts entered into between the parties creates legal obligations on
                    either sides and hence give a fair view of the impact of such contracts on
                    the&nbsp;day-to-day&nbsp;business operations as well as&nbsp;long-term&nbsp;viability and future
                    growth prospects. Hence, reviews of such contracts becomes a legal necessity which includes:</p>
                <p>Ø&nbsp;Suppliers contracts</p>
                <p>Ø&nbsp;Shareholders Agreement</p>
                <p>Ø&nbsp;Asset purchase Agreement</p>
                <p>Ø&nbsp;Loans, Guarantee and credit arrangements</p>
                <p>Ø&nbsp;Partnership and Joint Venture Agreements</p>
                <p>Ø&nbsp;Settlement Agreements</p>
                <p>Ø&nbsp;Non-compete Agreements</p>
                <p>Ø&nbsp;Franchise Agreements</p>
                <p>Ø&nbsp;Employment Agreements</p>
                <p>Ø&nbsp;Works contract</p>
                <p>Ø&nbsp;Security Agreement</p>
                <h4><strong><u>Litigation</u></strong></h4>
                <p>As aware, since the Companies&nbsp;enter into&nbsp;various aforesaid contracts, it is&nbsp;to
                    be&nbsp;seen&nbsp;whether&nbsp;any dispute exists between the parties&nbsp;to&nbsp;any binding terms
                    of such contracts. Litigation is a prospect that determines the value earned and lost in the market
                    due to existing dispute. Therefore, any existing dispute between the parties could be a big hit for
                    the growth and value prospect of the Corporate. Supposedly, if any management dispute exists, in
                    such a case the&nbsp;day-to-day&nbsp;working would be affected that would indirectly take a toll on
                    the revenue generating capacity of the Company due to poor management. Therefore, litigations
                    arising out of any contract or arrangement needs&nbsp;to be&nbsp;taken care of either by amicable
                    settlement or by way of court proceedings.</p>
                <h4><strong><u>Summing up</u></strong></h4>
                <p>Due diligence is an investigation to target any risk from a legal perspective. This process occurs
                    before acquiring a business or company. The purpose is to have knowledge of the risks&nbsp;prior
                    to&nbsp;purchase.</p>
                <p>From a legal viewpoint, the more knowledge&nbsp;of&nbsp;the business, helps a buyer to make an
                    informed decisions.</p>
                <p>The result of legal due diligence will help explain the current situation of the
                    business,&nbsp;identify&nbsp;the risks and structure the acquisition. Understanding, the
                    organization and the exposure it reflects to the outside world, both in Goodwill and financial
                    aspect are an important aspects for a buyer to keep in mind before entering the deal. Soundness of
                    business model and well complied structure would be an edge that could lead to creation of various
                    synergies.&nbsp;&nbsp;Due diligence help investors to get an&nbsp;accurate&nbsp;view on what the
                    Company has done so far and how it might fit into a broad portfolio or investment strategy.</p>

