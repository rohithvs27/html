                <h4><span>Benefits of Having a PAN Card</span></h4>
                <h5><span><strong>1. IT Returns Filing</strong></span></h5>
                <p>All individuals and entities who are eligible for Income tax are expected to file their IT returns. A
                    PAN
                    card is necessary for filing of IT returns, and is the primary reason individuals, as well as other
                    entities, apply for one.</p>
                <h5><span><strong>2. Identity Proof</strong></span></h5>
                <p>PAN card serves a valid identity proof. Other than voter card, Aadhaar card, PAN card is also
                    accepted by
                    all financial institutions and other organisations as an identity proof.</p>
                <h5><span><strong>3.Tax Deductions</strong></span></h5>
                <p>One of the essential reasons to get a PAN card is for the purpose of taxation.</p>
                <p>If the entity or person has not linked the PAN number with his bank account, and the annual interest
                    earnings on savings deposits is more than Rs. 10,000/- then the bank would deduct 30% TDS instead of
                    10%.</p>
                <h5><span><strong>4.Claiming Income Tax Refund</strong></span></h5>
                <p>Many times, the TDS deducted from a taxpayer’s income is more than the actual tax that he is supposed
                    to
                    pay. In order to claim excess tax paid, taxpayer has to have a PAN linked to his bank account.</p>
                <h5><span><strong>5.Starting a Business </strong></span></h5>
                <p>In order to start a business, a company or any other entity mandatorily needs to have a PAN
                    registered in
                    the name of its entity.</p>
                <p>Tax Registration Number (TRN) is needed for a business which is obtained only if the entity has a
                    PAN.
                </p>
                <h5><span><strong>6. Opening a Bank Account</strong></span></h5>
                <p>All public, private and co-operative banks have made it mandatory for an individual or a company to
                    have
                    a bank account in its name in order to open a savings or current account.</p>
                <p>Only under Pradhan Mantri Jan Dhan Yojana, a person can open a zero balance account by using voter
                    card
                    or other identity proof.</p>
                <h5><span><strong>7.Opening Demat Account</strong></span></h5>
                <p>PAN card is also mandatory for an individual or entity to open a Demat Account, which is needed to
                    hold
                    shares in dematerialized form.</p>
                <h5><span><strong>8.Purchase &amp; Sale of Immovable
                            Assets</strong></span></h5>
                <p>One of the benefits of pan card is eligibility of an individual or entity to enter into transactions
                    of
                    sale or purchase of assets.</p>
                <p>PAN is required to be quoted in the deed made during sale or purchase of any immovable property
                    valued at
                    10 lakh rupees or more.</p>
                <h5><span><strong>9.Foreign Travel</strong></span></h5>
                <p>Cash payment related with foreign travel of an amount more than Rs. 50,000/- inclusive of forex
                    purchase
                    requires PAN card</p>
                <h5><span><strong>10.Time Deposit</strong></span></h5>
                <p>Deposits with post office, co-operative banks, non-banking financial companies, will also need PAN,
                    deposits adding up to Rs. 5 lakh or more every year will require PAN</p>
                <h5><span><strong>11.Hotel and Restaurant Bills</strong></span></h5>
                <p>Payments more than Rs. 50,000/- for hotel and restaurant bills requires PAN to be given</p>
                <h5><span><strong>12.Cash Cards and Pre-Paid
                            Instruments</strong></span></h5>
                <p>Cash payment adding up to more than Rs. 50,000/- per year will need PAN to be furnished</p>
                <h5><span><strong>13. Purchase or Sale of Goods and
                            Services</strong></span></h5>
                <p>Purchase or sale of any good or service for an amount more than Rs. 2 lakh per transaction will
                    require
                    PAN to be furnished</p>
                <h5><span>14. Bank Drafts, Pay Orders and Banker’s
                        Cheque</span></h5>
                <p>PAN card is needed for Bank Drafts, Pay Orders and Banker’s Cheque with a transaction more than Rs
                    50000/- in a day. This implies that the transactions more than 50000/- will only need PAN card.</p>
                <h4><span><strong>Benefits to Income Tax Authority</strong></span>
                </h4>
                <p>1. A PAN card helps the Income-tax Authority in keeping a track of all financial transactions that
                    may be
                    essential in assessing an individual or companies tax liability, it helps in reducing the chances of
                    tax
                    evasion. The pan card has the person’s name, date of birth and photograph and is also accepted as an
                    identity proof for its holder.</p>
                <p>2. The PAN number is unique to every entity who holds a PAN card. The name and address may change or
                    the
                    card may get lost or stolen but the PAN number will still remain unchanged. So, the possibility of
                    misuse is minimal.</p>
                <p>3. A minor account can be opened easily with the PAN card of his or her guardian</p>
                <p>4. It is a useful tool to anticipate the total tax revenue being generated in India</p>
                <p>5. It helps in fixing the rate at which an entity should be taxed based on its earnings</p>
