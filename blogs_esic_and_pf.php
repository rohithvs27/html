                            <p style><strong><u>Applicability of EPF Registration for Employers</u></strong></p>
                            <p>EPF registration is mandatory for all establishments-</p>
                            <ul>
                                <li>Which is a factory engaged in any industry having 20 or more persons, and</li>
                                <li>To any other establishment employing 20 or more persons or class of such
                                    establishments which the Central Government may, by notification specify in this
                                    behalf.</li>
                            </ul>
                            <p>Central Government may apply any establishment employing less than 20 employees after
                                giving not less than two months’ notice for compulsory registration</p>
                            <p>Where the employer and majority of employees have agreed that the provisions of this act
                                should be made applicable to the establishment, they may themselves apply to the Central
                                PF Commissioner. The Central PF Commissioner may apply the provisions of this Act to
                                that establishment after passing the notification in the Official Gazette from the date
                                of such agreement or from any subsequent date specified in the agreement.</p>
                           

