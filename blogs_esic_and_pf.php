                2. The employee can pay at a higher rate and in such case employer is not under any obligation to pay
                at such higher rate.</p>
                3. To pay contribution on higher wages, a joint request from Employee and employer is required [Para
                26(6) of EPF Scheme]. In such case employer has to pay administrative charges on the higher wages (wages
