            <h4>Key Highlights of Indian Companies Act 2013</h4>
            <ul>
                <li>The maximum number of members (shareholders) permitted for a Private Limited Company is increased to 200 from 50.</li>
                <li>One-Person company.</li>
                <li>Section 135 of the Act which deals with Corporate Social Responsibility.</li>
                <li>Company Law Tribunal and Company Law Appellate Tribunal.</li>
            </ul>
            <h4>Salient features of the Companies Act 2013</h4>
            <img src="assets/img/blog/indian_act.jpg" alt="">

            <ul>
                <li><b>Class action suits for Shareholders:</b>The Companies Act 2013 has introduced new concept of class action suits with a view of making shareholders and other stakeholders, more informed and knowledgeable about their rights.</li>
                <li><b>More power for Shareholders:</b>The Companies Act 2013 provides for approvals from shareholders on various significant transactions.</li>
                <li><b>Women empowerment in the corporate sector:</b>The Companies Act 2013 stipulates appointment of at least one woman Director on the Board (for certain class of companies).</li>
                <li><b>Corporate Social Responsibility</b>The Companies Act 2013 stipulates certain class of Companies to spend a certain amount of money every year on activities/initiatives reflecting Corporate Social Responsibility.</li>
                <li><b>National Company Law Tribunal</b>The Companies Act 2013 introduced National Company Law Tribunal and the National Company Law Appellate Tribunal to replace the Company Law Board and Board for Industrial and Financial Reconstruction. They would relieve the Courts of their burden while simultaneously providing specialized justice.</li>
                <li><b>Fast Track Mergers</b>The Companies Act 2013 proposes a fast track and simplified procedure for mergers and amalgamations of certain class of companies such as holding and subsidiary, and small companies after obtaining approval of the Indian government.</li>
            </ul>
