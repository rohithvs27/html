                    <p><strong><span style="text-decoration: underline;">What is arm’s length price:</span></strong> As
                        stated above the income from an international transaction is computed as per arm’s length price.
                        Arm’s length price has been defined in section 92F(ii) as a price which is applied or proposed
                        to be applied in a transaction between persons other than associated enterprises, in
                        uncontrolled conditions.</p>
                    <p>The arm’s length principle suggests that the associated enterprises should be treated as
                        independent enterprises and international transaction is considered at the market price.</p>
                    <p>As per section 92C the arm’s length price in relation to an international transaction shall be
                        determined by any of the prescribed methods discussed as below:</p>
                    <p><strong>Comparable Uncontrolled Prices method (CUP):</strong> Under this method, price charged in
                        an uncontrolled transaction between comparable entities is identified and compared with the
                        tested entity price (after making due adjustments in relation to terms and conditions and risk
                        involved) to determine the ALP.</p>
                    <p><strong>Cost Plus Method (CPM):</strong> Here, the total cost of production incurred by the
                        enterprise in question in transferring goods and services to Associated Enterprises (AEs) is
                        calculated and the total gross profit mark up used by comparable entities in similar
                        transactions with independent enterprises is determined. The total gross mark-up arrived at is
                        adjusted to take into account functional and other differences and is added to the costs
                        calculated to determine ALP.</p>
                    <p><strong>Resale Price Method (RPM):</strong> Under this method the price at which property or
                        services in question are resold or provided to an unrelated enterprise is identified. Such
                        resale price is reduced by the normal gross profit accruing to an enterprise in a comparable
                        incontrolled transaction.</p>
                    <p><strong>Profit Split Method (PSM):</strong> PSM is used when AEs transactions are so integrated
                        that it becomes impossible to conduct a TP analysis on a transactional basis. First, the
                        combined net profit incurring to related enterprises from a transaction is determined. Then, the
                        combined net profit is allocated between related enterprises with reference to market returns
                        achieved by independent entities in similar transactions. The relative contribution of related
                        parties is then evaluated on the basis of assets employed, functions performed or to be
                        performed and risk assumed.</p>
                    <p><strong>Transactional Net Margin Method (TNMM):</strong> TNMM is normally adopted in cases of
                        transfer of semi-finished goods, distribution of finished products (where resale price method
                        (RPM) cannot be adequately applied) and transactions involving the provision of services. TNMM
                        compares the net profit margin relative to an appropriate base (sales, assets or costs incurred)
                        of the tested party with net profit margin of the independent enterprises in similar
                        transactions after making adjustments.</p>
                    <p><strong>Any other method as may be prescribed by the Board</strong></p>
                    <p>The most appropriate method of all the above methods should be applied in determining arm’s
                        length price depending upon various factors like nature of transaction availability of
                        information etc.</p>
                    <p><strong>Where more than one price is determined by the most appropriate method, </strong>the
                        arm’s length price shall be taken to be the arithmetical mean of such prices.</p>
                    <p>Actual price at which international transaction has been undertaken shall be accepted where the
                        variation with the arm’s length price doesn’t exceed 5 percent.</p>
                    <p>Section 92CB provides that w.e.f. asst. year 2009-10 Income tax authorities will accept the
                        transfer price declared by the assessee provided they are in accordance with the Safe Harbor
                        Rules to be notified by CBDT. Safe Harbour means circumstances in which the income tax
                        authorities shall accept the transfer price declared by the assessee.</p>
                    <p>When the assessing officer can determine arm’s length price: As per section 92C(3)</p>
                    <p>Where during the course of any proceeding for the assessment of income, the Assessing Officer is,
                        on the basis of material or information or document in his possession, of the opinion that—</p>
                    <p>(<em>a</em>)&nbsp; the price charged or paid in an international transaction has not been
                        determined in accordance with the prescribed manner; or</p>
                    <p>(<em>b</em>)&nbsp; any information and document relating to an international transaction have not
                        been kept and maintained by the assessee in accordance with the provisions contained in
                        sub-section (1) of section 92D and the rules made in this behalf; or</p>
                    <p>(<em>c</em>)&nbsp; the information or data used in computation of the arm’s length price is not
                        reliable or correct; or</p>
                    <p>(<em>d</em>)&nbsp; the assessee has failed to furnish, within the specified time, any information
                        or document which he was required to furnish by a notice issued under sub-section (3) of section
                        92D,</p>
                    <p>the Assessing Officer may proceed to determine the arm’s length price in relation to the said
                        international transaction on the basis of the information or material available.</p>
                    <p><strong>Procedure to be followed by the assessing officer while determining arm’s length
                            price:</strong> Proviso to section 92C(3) provides that an opportunity shall be given by the
                        Assessing Officer by serving a notice calling upon the assessee to show cause, on a date and
                        time to be specified in the notice, why the arm’s length price should not be so determined on
                        the basis of material or information or document in the possession of the Assessing Officer.</p>
                    <p>The Finance Act 2002 has inserted a new section 92CA according to which in case of international
                        transaction, the A.O may refer the matter to Transfer Pricing officer, if he considers it
                        necessary or expedient to do so, with the prior approval of commissioner, for the purpose of
                        computing arm’s length price.</p>
                    <p>The following procedure will be followed by TPO while determining ALP:</p>
                    <p>(1) Where a reference is made to the TPO, the Transfer Pricing Officer shall serve a notice on
                        the assessee requiring him to produce or cause to be produced on a date to be specified therein,
                        any evidence on which the assessee may rely in support of the computation made by him of the
                        arm’s length price in relation to the international transaction in question.</p>
                    <p>(2) On the date specified in the notice , or as soon thereafter as may be, after hearing such
                        evidence as the assessee may produce, including any information or documents referred to in
                        sub-section (3) of section 92D and after considering such evidence as the Transfer Pricing
                        Officer may require on any specified points and after taking into account all relevant materials
                        which he has gathered, the Transfer Pricing Officer shall, by order in writing, determine the
                        arm’s length price in relation to the international transaction and send a copy of his order to
                        the Assessing Officer and to the assessee.</p>
                    <p>(3) On receipt of the order under from TPO, the Assessing Officer shall proceed to compute the
                        total income of the assessee &nbsp;in conformity with the arm’s length price as so determined by
                        the Officer.</p>
