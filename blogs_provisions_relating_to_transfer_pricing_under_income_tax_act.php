                        cost or expense incurred or to be incurred in connection with a benefit, service or facility
                        provided or to be provided to any of such associated enterprises.</p>
                    <p>Further section 92B provides that, where a transaction entered into by an enterprise with a
                        person other than an associated enterprises and there exist a prior agreement in relation to the
                        relevant transaction between such other person and associated enterprise, or the terms of the
                        relevant transaction are determined in substance between such other person and the associated
                        enterprises then such transaction shall also be treated as an international transaction.</p>
                    <p>(a)&nbsp; which participates, directly or indirectly, or though one or more
                        intermediaries, in the management or control or capital of other enterprise; or</p>
                    <p>(b) in respect of which, one or more persons who participate directly or indirectly
                        or through one or more intermediaries, in its management or control or capital, are the same
                        persons who participate, directly or indirectly or through one or more intermediaries, in the
                        management or control or capital of the other enterprise.</p>
                    <p>Two enterprises shall be deemed to be associated enterprises if, at any time during the previous
                        year,-</p>
                    <p>(a)&nbsp; One enterprise holds, directly or indirectly, shares carrying not less than
