                            transaction.</p>
                        <p><strong>Profit Split Method (PSM):</strong> PSM is used when AEs transactions
                            are so integrated that it becomes impossible to conduct a TP analysis on a
                            transactional basis. First, the combined net profit incurring to related
                            enterprises from a transaction is determined. Then, the combined net profit
                            is allocated between related enterprises with reference to market returns
                            achieved by independent entities in similar transactions. The relative
                            contribution of related parties is then evaluated on the basis of assets
                            employed, functions performed or to be performed and risk assumed.</p>
                        <p><strong>Transactional Net Margin Method (TNMM):</strong> TNMM is normally
                            adopted in cases of transfer of semi-finished goods, distribution of
                            finished products (where resale price method (RPM) cannot be adequately
                            applied) and transactions involving the provision of services. TNMM compares
                            the net profit margin relative to an appropriate base (sales, assets or
                            costs incurred) of the tested party with net profit margin of the
                            independent enterprises in similar transactions after making adjustments.
                        </p>
                        <p><strong>Any other method as may be prescribed by the Board</strong></p>
                        <p>The most appropriate method of all the above methods should be applied in
                            determining arm’s length price depending upon various factors like nature of
                            transaction availability of information etc.</p>
                        <p><strong>Where more than one price is determined by the most appropriate
                                method, </strong>the arm’s length price shall be taken to be the
                            arithmetical mean of such prices.</p>
                        <p>Actual price at which international transaction has been undertaken shall be
                            accepted where the variation with the arm’s length price doesn’t exceed 5
                            percent.</p>
                        <p>Section 92CB provides that w.e.f. asst. year 2009-10 Income tax authorities
                            will accept the transfer price declared by the assessee provided they are in
                            accordance with the Safe Harbor Rules to be notified by CBDT. Safe Harbour
                            means circumstances in which the income tax authorities shall accept the
                            transfer price declared by the assessee.</p>
                        <p>When the assessing officer can determine arm’s length price: As per section
                            92C(3)</p>
                        <p>Where during the course of any proceeding for the assessment of income, the
                            Assessing Officer is, on the basis of material or information or document in
                            his possession, of the opinion that—</p>
                        <p>(<em>a</em>)&nbsp; the price charged or paid in an international transaction
                            has not been determined in accordance with the prescribed manner; or</p>
                        <p>(<em>b</em>)&nbsp; any information and document relating to an international
                            transaction have not been kept and maintained by the assessee in accordance
                            with the provisions contained in sub-section (1) of section 92D and the
                            rules made in this behalf; or</p>
                        <p>(<em>c</em>)&nbsp; the information or data used in computation of the arm’s
                            length price is not reliable or correct; or</p>
                        <p>(<em>d</em>)&nbsp; the assessee has failed to furnish, within the specified
                            time, any information or document which he was required to furnish by a
                            notice issued under sub-section (3) of section 92D,</p>
                        <p>the Assessing Officer may proceed to determine the arm’s length price in
                            relation to the said international transaction on the basis of the
                            information or material available.</p>
                        <p><strong>Procedure to be followed by the assessing officer while determining
                                arm’s length price:</strong> Proviso to section 92C(3) provides that an
                            opportunity shall be given by the Assessing Officer by serving a notice
                            calling upon the assessee to show cause, on a date and time to be specified
                            in the notice, why the arm’s length price should not be so determined on the
                            basis of material or information or document in the possession of the
                            Assessing Officer.</p>
                        <p>The Finance Act 2002 has inserted a new section 92CA according to which in
                            case of international transaction, the A.O may refer the matter to Transfer
                            Pricing officer, if he considers it necessary or expedient to do so, with
                            the prior approval of commissioner, for the purpose of computing arm’s
                            length price.</p>
                        <p>The following procedure will be followed by TPO while determining ALP:</p>
                        <p>(1) Where a reference is made to the TPO, the Transfer Pricing Officer shall
                            serve a notice on the assessee requiring him to produce or cause to be
                            produced on a date to be specified therein, any evidence on which the
                            assessee may rely in support of the computation made by him of the arm’s
                            length price in relation to the international transaction in question.</p>
                        <p>(2) On the date specified in the notice , or as soon thereafter as may be,
                            after hearing such evidence as the assessee may produce, including any
                            information or documents referred to in sub-section (3) of section 92D and
                            after considering such evidence as the Transfer Pricing Officer may require
                            on any specified points and after taking into account all relevant materials
                            which he has gathered, the Transfer Pricing Officer shall, by order in
                            writing, determine the arm’s length price in relation to the international
                            transaction and send a copy of his order to the Assessing Officer and to the
                            assessee.</p>
                        <p>(3) On receipt of the order under from TPO, the Assessing Officer shall
                            proceed to compute the total income of the assessee &nbsp;in conformity with
                            the arm’s length price as so determined by the
                            <strong> Transfer
                                Pricing</strong> Officer.</p>
                        <p><strong><span style="text-decoration: underline;">The obligations of an
                                    assessee having international transactions:</span></strong> The
                            obligations of an assessee having international transaction are as follows:
                        </p>
                        <p>(1) The income from the international transaction should be
                            computed as per arm’s length price</p>
                        <p>(2) Every person who has entered into an international
                            transaction shall keep and maintain such information and document in respect
                            thereof and for such period, as may be prescribed by the board and produce
                            before the A.O or commissioner (Appeals) as and when required in the course
                            of proceedings under Income Tax Act within a period of 30 days from the date
                            of receipt of notice.</p>
                        <p>(3) The assessee entering into an international transaction is
                            also required to furnish an audit report in the form 3CEB by a chartered
                            accountant by 31<sup>st</sup> of September of relevant A.Y where the
                            assessee is a company and by 31<sup>st</sup> day of July in other cases.</p>
                        <p><strong><span style="text-decoration: underline;">Penal
                                    provisions:</span></strong> As per section 271AA if the required
                            information and documents are not maintained, the A.O or CIT(A) may impose a
                            penalty of a sum equal to 2% of the value of such international transaction.
                            Similar penalty is provided u/s 271G if the assessee fails to furnish the
                            documents requisitioned by the A.O or CIT (A).</p>
                        <p>Section 271BA provides that if an assessee fails to furnish the report of
                            Chartered accountant as required u/s 92E, the A.O may impose penalty of Rs 1
                            Lakh.</p>
                        <p>Explanation 7 to section 271(1)(c)&nbsp; provides that where in the case of
                            an assessee who has entered into an international transaction defined in
                            section 92B, any amount is added or disallowed in computing the total income
                            under section 92C(4) then, the amount so added or disallowed shall, for the
                            purpose of section 271(1)(c) , be deemed to represent the income in respect
                            of which particulars have been concealed or inaccurate particulars have been
                            furnished.</p>
                        <p>However the explanation does not apply where the assessee proves to the
                            satisfaction of the A.O or the CIT that the price charged or paid in such
                            transaction has been determined in accordance with section 92C in good faith
                            and with due diligence.</p>
                        <p>As per section 273B no penalty will be levied u/s 271AA, 271BA, 271G where it
                            is proved that there was a reasonable cause for the failure.</p>


