                            International transactions, associated enterprises and arm’s length. These can be discussed
                            as follows</strong>:</p>
                    <p><strong><span style="text-decoration: underline;">What is an International
                                Transaction:</span></strong> To apply the provisions related to transfer pricing as
                        contained u/s 92, 92A to 92F there must be an international transaction.</p>
                    <p>As per section 92B an international transaction means a transaction between two or more
                        associated enterprises, either or both of whom are non-resident and such transaction is in the
                        nature of purchase, sale or lease of tangible or intangible property or provision of services,
                        or lending or borrowing money or any other transaction having a bearing on the profits, income,
                        losses or assets of such associated enterprises.</p>

