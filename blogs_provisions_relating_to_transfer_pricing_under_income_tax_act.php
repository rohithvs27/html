                    <p>(1) The income from the international transaction should be computed as per arm’s
                        length price</p>
                    <p>(2) Every person who has entered into an international transaction shall keep and
                        maintain such information and document in respect thereof and for such period, as may be
                        prescribed by the board and produce before the A.O or commissioner (Appeals) as and when
                        required in the course of proceedings under Income Tax Act within a period of 30 days from the
                        date of receipt of notice.</p>
                    <p>(3) The assessee entering into an international transaction is also required to
                        furnish an audit report in the form 3CEB by a chartered accountant by 31<sup>st</sup> of
