                <p>With the advent of MNCs(Multi National Concerns) a trend has also been adopted by the MNCs to
                    structure their investments and business strategy in such a way that profits are maximized in such
                    jurisdictions where tax rates are low, which give rise to the emerging&nbsp; problem of
                    <strong>transfer pricing</strong> all over the
                    world. Many countries have made laws to deal with the issue of transfer
                    pricing. India has been a late entrant in making provisions under the Income Tax Act to tackle the
                    issue of transfer pricing. Although there existed section 92 under Income tax Act 1961 but there
                    were not relevant rules which could help tackle the issue of transfer pricing. Section 92A to 92F
                    had been inserted to deal with transfer pricing by the Finance Act, 2001. Some views are expressed
                    in this article as below explaining provisions under the Income Tax Act 1961 dealing with transfer
                    pricing.</p>
                <p><strong>What is Transfer Pricing: </strong>Usually there is a tendency among MNCs to adjust their
                    international transactions in such a way that maximum profit arises in that country where the rate
                    of tax is lowest and minimum profit arises in that country where the rate of tax is highest. This
                    creates the problem of transfer pricing. Thus there may be chances that MNC&nbsp;escape tax in those
                    countries where the tax rate is more by adjusting their international transaction and declaring
                    lesser profits in such country. This can be explained with an example:</p>
                <p>Suppose a subsidiary company, resident in country A (which has a tax rate of, say, 30%) manufactures
                    goods and transfers them to its parent company in country B (which has a tax rate of 20%) for
                    trading. In order to increase the overall profits of the group company, it will seek to supply the
                    goods at prices which are lower than the market price. So, in effect, the subsidiary company in
                    country A will have lower profits and hence, a lower tax incidence whereas the parent company in
                    country B is affected in the opposite manner higher profits due to low costs, but lower taxes
                    because of the tax rate.</p>
                <p>Transfer pricing deals with the technique where parent companies sell goods and services to
                    subsidiary entities at an inflated price to deliberately reduce profits and tax liability. The law
                    requires that goods and services should be sold to subsidiary companies at arm’s length price — the
                    price at which goods are traded between unconnected companies.</p>
                <p>In order to cover such tendencies of MNCs section 92A to 92F have been enacted&nbsp;and section
                    271AA, 271BA and 271G have been incorporated and section 271 has been amended providing for penal
                    provisions in this regard.</p>
                <p><strong>How the income from international transactions will be computed:</strong> As per section 92
                    any income from international transaction shall be computed as per arm’s length price. Any allowance
                    of interest or expenses with respect to the above income shall also be computed having regarded to
                    arm’s length price.

                    <p>Further where, in an international transaction, two or more associated enterprises enter into a
                        mutual agreement for the allocation or apportionment of, or any contribution to, any cost or
                        expense incurred or to be incurred in connection with a benefit, service or facility provided or
                        to be provided or apportioned to or as the case may be, contributed by, any such enterprises
                        shall be determined having regard to the arm’s length price of such benefit, service or
                        facility, as the case may be.</p>
                    <p>In simple words it means that not only the income from an international transaction is to be
                        computed as per arm’s length price but also any expense or cost to be incurred in an
                        international transaction in connection with a benefit or service or facility to be provided
                        will be computed as per arm’s length price.</p>
                    <p>Section 92 also provides that its provisions shall not apply where it has effect of reducing the
                        income chargeable to tax or increasing the loss, as the case may be, computed on the basis of
                        entries made in the books of account in respect of the previous year in which the international
                        transaction was entered into.</p>
