                    <h4>Treatment of existing AEOs and ACPs</h4>
                    <h5>Existing AEOs</h5>
                    <ul>
                        <li>Existing AEOs shall be granted either AEO- T2 or AEO-LO Certificate depending upon
                            their category by the AEO Programme Manager within 15 days of issue of this circular.</li>
                        <li>They shall remain AEO-T2 or AEO-LO certificate holders till the validity period mentioned in
                            their AEO certificate issued under Circular 28/2012 –Cus dated
                            16-11-2012. Thereafter, they will have to follow renewal procedure.
                        </li>
                    </ul>
                    <h5>Existing ACPs</h5>
                    <ul>
                        <li>Existing ACP shall be <strong><b>PROVISIONALLY </b></strong>granted AEO- T1
                            Certificate by the AEO Programme Manager within 15 days of issue of this circular.</li>
                        <li>All such ACP clients shall have an opportunity to submit applications for
                            <strong><b>REGULARIZATION </b></strong>of AEO Certificates in terms of this circular within
                            90 days of issue of this circular. Who fail to submit application within 90 days shall be
                            taken up for review.</li>
                        <li>They shall remain AEO-T1 certificate holders till the validity period mentioned in
                            their ACP renewal /granting letter. Thereafter, they will have to follow renewal procedure
                            as prescribed in this circular.</li>
                    </ul>
                    <h5>Who is entitled for AEO Certification</h5>
                    <ul>
                        <li>Anyone involved in the international supply chain that undertakes Customs related
                            activity in India can apply for AEO status irrespective of size of the business. These may
                            include exporters, importers, logistic providers (e.g. carriers, airlines, freight
                            forwarders, etc.), Custodians or Terminal Operators, Customs House Agents and Warehouse
                            Owners. Others who may qualify include port operators, authorized couriers, stevedores. The
                            list is not exhaustive.</li>
                    </ul>
                    <h5>AEO for SME Enterprises</h5>
                    <ul>
                        <li>Small and medium scale enterprises who has filed 25 documents i.e. either Bills of
                            Entry or Shipping Bills during the last financial year is also entitled for AEO.</li>
                    </ul>
                    <h5>AEO is applicable to OLD Company or new company</h5>
                    <ul>
                        <li>The applicant should have business activities for <strong><b>at least three financial
                                    years preceding the date of application.</b></strong></li>
                    </ul>
                    <h5>Who is not entitled for AEO certification</h5>
                    <p>Businesses that are not engaged in Customs related work / activities will not be entitled to
                        apply, in general, banks, insurance companies, consultants and the like categories of businesses
                        will not be eligible for AEO status.</p>
                    <h5>AEO is branch wise or entity wise</h5>
                    <ul>
                        <li>AEO certification is entity wise not branch/location/group wise. If a group of
                            company have 5 companies then all company will have to apply separately.</li>
                    </ul>
                    <h5>AEO status of Customs Broker and AEO status of Importer/Exporter are
                        Independent</h5>
                    <ul>
                        <li>AEO status can be granted to a Customs Broker, but this will not confer similar status on
                            its client importers / exporters who will need to apply separately for that status.</li>
                    </ul>
                    <h5>AEO Is Applicable To OLD Company Or New Company</h5>
                    <ul>
                        <li>The applicant should have business activities for at least three financial years preceding
                            the date of application in exceptional circumstances on physical verification of control and
                            premises may be allowed to new entity.</li>
                    </ul>
