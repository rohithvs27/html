                <div><strong><u><b>What is AEO (Authorized Economic Operator) </b></u></strong></p>
                    <p>AEO is a trade facilitation scheme for ease of doing business in light of international
                        development, holder of this certificate is entitled for privilege, benefits, exemption and
                        relaxation on account of import and export. This certificate is issued for particular period
                        after that to be renewed.</p>
                    <p>This scheme is based on principle of sharing role and responsibility of customs with trade and
                        industry and objective is to delink payment and clearance, to accept paperless declaration,
                        increases efficiency, self certification, earliest refund and drawback, request based
                        examination/inspection etc.</p>
                    <p>ACP (accredited client program) and old AEO (authorized economic operator) is merged together and
                        resulted into three certification namely AEO-T1, AEO-T2, AEO-T3, AEO-LO.</p>
