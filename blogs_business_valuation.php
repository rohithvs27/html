                </p>
                <h4>Special Considerations: Methods of Valuation</h4>
                <p>
                    There are numerous ways a company can be valued. You'll learn about several of these methods below.
                </p>

                <h5> 1. Market Capitalization</h5>
                <p>
                    Market
                    capitalization is the simplest method of business valuation. It is calculated by multiplying the
                    company’s share price by its total number of shares outstanding. For example, as of January 3, 2018,
                    Microsoft
                    Inc. traded at $86.35.1 With a total number of shares outstanding of 7.715 billion, the
                    company could then be valued at $86.35 x 7.715 billion = $666.19 billion.
                </p>

                <h5>2. Times Revenue Method </h5>
                <p>
                    Under the times
                    revenue business valuation method, a stream of revenues generated over a certain period of time is
                    applied to a multiplier which depends on the industry and economic environment. For example, a tech
                    company may
                    be valued at 3x revenue, while a service firm may be valued at 0.5x revenue.
                </p>
                <h5>3. Earnings Multiplier</h5>
                <p>
                    Instead of the times revenue method, the earnings multiplier may be used to get a more accurate
                    picture of the real
                    value of a company, since a company’s profits are a more reliable indicator of its financial success
                    than sales
                    revenue is. The earnings multiplier adjusts future profits against cash flow that could be invested
                    at the
                    current interest rate over the same period of time. In other words, it adjusts the current P/E ratio
                </p>
                <h5>4. Discounted Cash Flow (DCF) Method </h5>
                <p>
                    The DCF method of business valuation is
                    similar to the earnings multiplier. This method is based on projections of future cash flows, which
                    are adjusted
                    to get the current market value of the company. The main difference between the discounted cash flow
                    method and
                    the profit multiplier method is that it takes inflation into consideration to calculate the present
                    value.
                </p>
                <h5>5. Book Value</h5>
                <p>
                    This is the value of shareholders’ equity of a business as shown on the balance sheet statement. The
                    book value is derived by subtracting
                    the total liabilities of a company from its total assets.
                </p>
                <h5>6. Liquidation Value</h5>
                <p>
                    Liquidation
                    value is the net cash that a business will receive if its assets were liquidated and liabilities
                    were paid off
                    today.
                </p>
                <p>
                    This&nbsp;is by no means an exhaustive list of the business valuation methods in use today. Other
                    methods
                    include replacement value, breakup value, asset-based valuation&nbsp;and still many more.
                </p>
