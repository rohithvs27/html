                <h4>The Basics of Business Valuation </h4>
                <p id="mntl-sc-block_1-0-8" class="comp mntl-sc-block finance-sc-block-html mntl-sc-block-html">
                    The topic of business valuation is frequently discussed in corporate finance. Business valuation is
                    typically
                    conducted when a company is looking to sell all or a portion of its operations or looking to merge
                    with or
                    acquire another company. The valuation of a business is the process of determining the current worth
                    of a
                    business, using objective measures,&nbsp;and evaluating all aspects of the business.
                </p>
                <p>
                    A business valuation might include an analysis of the company's management, its capital
                    structure</a>, its future earnings prospects&nbsp;or the market value of its assets. The tools used
                    for
                    valuation can vary among evaluators, businesses, and industries. Common approaches to business
                    valuation include
                    a review of financial statements, discounting cash flow models&nbsp;and similar company comparisons.
                </p>
                <p>
                    Valuation is also important for tax reporting. The Internal Revenue Service (IRS) requires that a
                    business is
                    valued based on its fair market value. Some tax-related events such as sale, purchase&nbsp;or
                    gifting of shares
                    of a company will be taxed depending on valuation.
                </p>

                <p>
                    <p>Estimating the fair value of a business is an art and a science; there are several formal models
                        that
